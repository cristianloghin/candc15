		<div class="h_2">Share-based rewards – new long term incentive plans</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Purpose and link to strategy</div>
		<div>To incentivise executive Directors to execute the Group’s business strategy over the longer term and align their interests with those of shareholders to achieve a sustained increase in shareholder value.</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Operation</div>
		<div>
			<p>Subject to shareholder approval at the 2015 AGM, a new Long Term Incentive Plan (“LTIP 2015”) and a new Executive Share Option Scheme (“ESOS 2015”) will be adopted. Awards will not be granted under the new arrangements until FY2017.</p>
			<p>Subject to the plan limits set out below the Committee has the discretion to determine the appropriate mix of LTIP 2015 and ESOS 2015 awards each year in the context of the Company’s business cycle and its future growth plans save where the executive has a contractual entitlement. Malus and clawback provisions will apply to both the LTIP 2015 and the ESOS 2015. See the “Malus and clawback” section below for more details.</p>
			<p>Options/awards will be granted under the existing ESOS and the LTIP (Part I) in FY2016 (see Implementation section below).</p>
			<p>Awards are usually made annually by the Committee following the release of full year financial results but can be made after release of the interim results and exceptionally at other times.</p>
		</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Opportunity</div>
		<div>If awards are made under both the LTIP 2015 and the ESOS 2015 in respect of the same financial year the overall maximum, other than in exceptional circumstances, will be capped at 250% of salary. In exceptional circumstances the maximum combined LTIP 2015 and ESOS 2015 award in respect of any financial year is 500% of salary.</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Performance metrics</div>
		<div>The vesting of awards is subject to the satisfaction of performance conditions set by the Committee. Performance conditions are selected that are aligned to the Company’s strategy and with shareholders’ interests. The performance measures chosen are reviewed regularly to ensure they remain relevant. The relevant measures, targets and weightings may vary each year based upon the Company’s priorities. Options lapse if the performance target threshold is not met in the relevant testing period and there is no retesting.</div>
	</div>
