		<div class="h_2">(a) ESOS and ESOS 2015</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Purpose and link to strategy</div>
		<div>To incentivise executive Directors to execute the Group’s business strategy over the longer term and align their interests with those of shareholders to achieve a sustained increase in shareholder value.</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Operation</div>
		<div>
			<p>The Committee may grant options to acquire shares in the Company at a market related exercise price. The Committee has discretion to grant ESOS 2015 awards to reward sustained value creation by averaging the value of the shares at grant and the point of exercise across an extended period of up to six months.</p>
			<p>The vesting of options is subject to meeting a specific performance target set by the Committee and measured over a period of three years. Options will not normally be exercisable until after the assessment of the performance condition following the end of the performance period.</p>
			<p>Early vesting may be available for certain qualifying leavers. See policy on payment for loss of office further down for more details.</p>
			<p>Options vest early on a change of control (or other relevant event), taking into account the performance conditions. Options may be adjusted in the event of a variation of share capital in accordance with the scheme rules.</p>
			<p>The Committee has the discretion to grant ESOS and ESOS 2015 options as tax-advantaged options as permitted by the UK Revenue authorities and allows grants of options over shares with a market value of up to the value prescribed by the applicable tax legislation (currently £30,000) to be made on a tax efficient basis to employees who are UK taxpayers. Tax-advantaged options will be subject to the same performance conditions as non-tax-advantaged ESOS options.</p>
		</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Opportunity</div>
		<div>
			<p>The maximum ESOS 2015 award is 150% of base salary in respect of any financial year if granted in combination with a LTIP 2015 award equal to 100% of salary.</p>
			<p>Other than in exceptional circumstances the limit on ESOS 2015 awards would be 300% of salary if no LTIP 2015 awards are granted in respect of the same financial year.</p>
			<p>This is subject to the overall exceptional circumstances limit set out above.</p>
