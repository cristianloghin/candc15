	<div class="row light_line">
		<div class="h_3">Operation</div>
		<div>
			<p>Under the LTIP (Part I) awards are granted in the form of nominal cost options to acquire shares or conditional awards. Under the LTIP 2015, awards of conditional shares, restricted stock or nil cost or nominal cost options (or similar cash equivalent) can be made.</p>
			<p>The vesting of awards is subject to meeting specific performance targets set by the Committee and measured over a period of three years. Awards will not normally vest until after the assessment of the performance condition following the end of the performance period.</p>
			<p>The Committee may decide that a participant has a right to ‘dividend equivalents’ whereby the participant receives additional value equivalent to that which accrues to shareholders by way of dividends that would have been paid on the underlying shares during the vesting period. This value can be paid as cash or shares.</p>
			<p>Early or pro-rata vesting may be available for certain qualifying leavers. See policy on payment for loss of office further down for more details.</p>
			<p>Awards vest early on a change of control (or other relevant event) taking into account the performance conditions and pro-rating for time, although the Committee has discretion not to apply time pro-rating. Awards may be adjusted in the event of a variation of share capital in accordance with the scheme rules.</p>
		</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Opportunity</div>
		<div>
			<p>The maximum LTIP 2015 award is 100% of base salary in respect of any financial year if granted in combination with an ESOS 2015 award equal to 150% of salary.</p>
			<p>The maximum LTIP 2015 award is 150% of base salary in respect of any financial year if no ESOS 2015 award is granted in respect of the same financial year.</p>
			<p>This is subject to the overall exceptional circumstances limit set out above.</p>
			<p>Contractual entitlements to be granted in FY2016 under the current LTIP are:<br>
			Stephen Glancey - LTIP (Part I): 100% of base salary<br>
			Kenny Neison - LTIP (Part I): 100% of base salary<br>
			Joris Brams - LTIP (Part I): 100% of aggregate base salary</p>
