		<div class="h_3">Element</div>
		<div class="h_2">Salary</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Purpose and link to strategy</div>
		<div>Purpose is to attract, recruit and retain Directors of the necessary calibre.</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Operation</div>
		<div>
			<p>Salary levels are determined by the Committee taking into account factors including:</p>
			<ul class="list">
				<li>scope and responsibilities of the role;</li>
				<li>experience and individual performance;</li>
				<li>overall business performance;</li>
				<li>prevailing market conditions;</li>
				<li>pay in comparable companies, principally in the global beverage sector; and</li>
				<li>overall risk of non-retention.</li>
			</ul>
		</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Opportunity</div>
		<div>
			<p>Executive Directors are entitled to an annual review of their salary, but there is no entitlement to receive any increase.</p>
			<p>The Committee may award salary increases to take account of individual circumstances such as:</p>
			<ul class="list">
				<li>increases or changes in scope and responsibility;</li>
				<li>to reflect the executive Director’s development and performance in the role; or</li>
				<li>alignment to market level.</li>
			</ul>
			<p>In awarding increases, the Committee will have regard to the outcome of pay reviews for employees as a whole.</p>
			<p>The base salaries effective as at 1 March 2015 are shown further down.</p>
		</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Performance metrics</div>
		<div>Not applicable.</div>
