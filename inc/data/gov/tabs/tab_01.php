		<div class="h_2">Benefits/cash allowance in lieu</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Purpose and link to strategy</div>
		<div>Purpose is to attract, recruit, and retain Directors of the necessary calibre.</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Operation</div>
		<div>The Group seeks to bring transparency to Directors’ reward structures through the use of cash allowances in place of benefits in kind. The cash allowance can be applied to benefits such as a company car and health benefits. Group benefits such as death in service insurance are also made available. Other benefits may be provided based on individual circumstances including housing or relocation allowances, travel allowance or other expatriate benefits. Benefits and allowances are reviewed alongside salary.</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Opportunity</div>
		<div>
			<p>The Committee has not set an absolute maximum on the levels of benefits that may be awarded since this will depend upon the circumstances applicable to the relevant Director as well as the cost of any third party suppliers. The value of the cash allowance/benefit is set at a level which the Committee considers appropriate against the market and provides sufficient level of benefit based on individual circumstances.</p>
			<p>See ‘Implementation’ section below for details of the benefits for FY2016.</p>
		</div>
	</div>
