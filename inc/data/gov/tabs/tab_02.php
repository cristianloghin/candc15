<div class="table col_2 text">
	<div class="row heavy_line">
		<div class="h_3">Element</div>
		<div class="h_2">Annual bonus</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Purpose and link to strategy</div>
		<div>Rewards performance against annual financial, operational and strategic business targets which support the strategic direction of the Company and align the interests of executives with shareholders.</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Operation</div>
		<div>
			<p>A discretionary scheme under which executive Directors are entitled to receive a variable reward contingent upon the achievement of performance targets.</p>
			<p>The structure and value of the bonus scheme and the applicable performance measures are subject to annual approval by the Committee. Any pay-out is determined by the Committee after the year end, based on performance against the relevant targets.</p>
			<p>The Committee has discretion to vary the bonus pay out should any formulaic output not reflect the Committee’s assessment of overall business performance.</p>
			<p>The Committee has discretion to apply deferral to part of any bonus earned in the year and for such amount to be deferred into shares for a period of up to two years.</p>
			<p>Malus and clawback provisions will apply to the annual bonus. See the ‘Malus and clawback’ section below for more details.</p>
			<p>The Committee reserves the right to vary, amend, replace or discontinue the bonus scheme at any time depending on business needs and/or financial viability or as appropriate by reference to any changes in corporate structure during the financial year.</p>
		</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Opportunity</div>
		<div>
			<p>Maximum opportunity is 100% of base salary.</p>
			<p>However, for FY2016 executive Directors are entitled to a maximum bonus opportunity of 80% of base salary.</p>
		</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Performance metrics</div>
		<div>
			<p>Measures and targets are set annually reflecting the Company’s strategy and aligned with key financial, operational, strategic and/or individual objectives.</p>
			<p>Targets, whilst stretching, do not encourage inappropriate business risks to be taken.</p>
			<p>The relevant measures and the respective weightings may vary each year based upon the Company’s priorities.</p>
			<p>If applicable, as the bonus is subject to performance measures, any deferred bonus is not subject to further performance conditions.</p>
			<p>See ‘Implementation’ section below for details of the bonus conditions for FY2016.</p>
		</div>
	</div>
</div>