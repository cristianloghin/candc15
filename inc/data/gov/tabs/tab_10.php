	<div class="row light_line">
		<div class="h_3">Purpose and link to strategy</div>
		<div>Sole element of non-executive Director remuneration is set at a level that reflects market conditions and is sufficient to attract individuals with appropriate knowledge and experience.</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Operation</div>
		<div>
			<p>Fees paid to non-executive Directors are determined and approved by the Board as a whole. The Committee recommends the remuneration of the Chairman to the Board.</p>
			<p>Fees are reviewed from time to time and adjusted to reflect market positioning and any change in responsibilities.</p>
			<p>Non-executive Directors receive a basic fee and an additional fee for further duties (for example chairmanship of a committee or senior independent Director responsibilities).</p>
			<p>Non-executive Directors are not eligible to participate in the annual bonus plan or share-based schemes and do not receive any benefits (including pension) other than fees in respect of their services to the Company.</p>
			<p>Non-executive Directors may be eligible to receive certain benefits as appropriate such as the use of secretarial support, travel costs or other benefits that may be appropriate.</p>
		</div>
	</div>
	<div class="row light_line">
		<div class="h_3">Opportunity</div>
		<div>
			<p>Fees are based on the level of fees paid to non-executive Directors serving on Boards of similar-sized Irish and UK-listed companies and the time commitment and contribution expected for the role.</p>
			<p>The Articles of Association provide that the ordinary remuneration of Directors (i.e. Directors’ fees, not including executive remuneration) shall not exceed a fixed amount or such other amount as determined by an ordinary resolution of the Company. The current limit was set at the Annual General Meeting held in 2013, when it was increased to €1.0 million in aggregate.</p>
