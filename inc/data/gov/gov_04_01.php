<h2>Consideration of employment conditions generally and consultation with employees</h2>
<p>As described above, when setting the policy for executive Directors’ remuneration, the Committee applies the same core principle as applied for the pay and employment conditions of other Group employees. When reviewing Directors’ remuneration, the Committee has regard to the outcome of pay reviews for employees as a whole.</p>
<p>The Committee did not directly consult with employees when formulating the Directors’ remuneration policy set out in this report and no remuneration comparison measurements comparing executive Directors remuneration with employees generally were used.</p>
<p>The Group has regular contact with employee representatives on matters of pay and remuneration for employees covered by collective bargaining or consultation arrangements.</p>
