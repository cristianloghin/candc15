<h2>RECRUITMENT REMUNERATION POLICY</h2>
<p>When recruiting a new executive Director, the Committee will typically seek to use the policy detailed in the table above to determine the appropriate remuneration package to be offered. To facilitate the hiring of candidates of the appropriate calibre required to implement the Group’s strategy, the Committee retains the discretion to make payments or awards which are outside the Policy subject to the principles and limits set out below.</p>
<p>In determining appropriate remuneration, the Committee will take into consideration all relevant factors (including the quantum and nature of remuneration) to ensure the arrangements are in the best interests of the Group and its shareholders. This may, for example, include (but is not limited to) the following circumstances:</p>
<ul class="list">
	<li>an interim appointment is made to fill an executive Director role on a short-term basis;</li>
	<li>exceptional circumstances require that the Chairman or a non-executive Director takes on an executive function on a short-term basis;</li>
	<li>an executive Director is recruited at a time in the year when it would be inappropriate to provide a bonus or long-term incentive award for that year as there would not be sufficient time to assess performance. Subject to the limit on variable remuneration set out below, the quantum in respect of the months employed during the year may be transferred to the subsequent year so that reward is provided on a fair and appropriate basis;</li>
	<li>the Executive received benefits at his previous employer which the Committee considers it appropriate to offer.</li>
</ul>
<p>The Committee may also alter the performance measures, performance period and vesting period of the annual bonus or long-term incentive, if the Committee determines that the circumstances of the recruitment merit such alteration. The rationale will be clearly explained.</p>
<p>The Committee may make an award to compensate the prospective employee for remuneration arrangements forfeited on leaving a previous employer. In doing so the Committee will take account of relevant factors regarding the forfeited arrangements which may include the form of any forfeited awards (e.g. cash or shares), any performance conditions attached to those awards (and the likelihood of meeting those conditions) and the time over which they would have vested. These awards or payments are excluded from the maximum level of variable remuneration referred to below; however, the Committee’s intention is that the value awarded or paid would be no higher than the expected value of the forfeited arrangements.</p>
<p>Recruitment awards will normally be liable to forfeiture or ‘clawback’ on early departure (i.e. within the first 12 months of employment).</p>
<p>It would be the Committee’s policy that a significant portion of the remuneration package (including any introductory awards) would be variable and linked to stretching performance targets and continued employment. The maximum level of variable remuneration that may be granted to new Directors (excluding buy-out arrangements) is 5 times base salary.</p>
<p>For the avoidance of doubt, if it were necessary to make an introductory cash award which was not performance-based, such as a guaranteed bonus payment, the Committee would include such an award within the overall limit of 5 times base salary. Where a position is filled internally, any pre-appointment remuneration entitlements or outstanding variable pay elements shall be allowed to continue according to the original terms.</p>
<p>Fees payable to a newly-appointed Chairman or non-executive Director will be in line with the fee policy in place at the time of appointment.</p>
