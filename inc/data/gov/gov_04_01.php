<h2>General statement of policy</h2>
<p>The main aim of the Group’s policy on Directors’ remuneration is to attract, retain and motivate Directors of the calibre required to promote the long-term success of the Group. The Committee therefore seeks to ensure that Directors are properly, but not excessively, remunerated and motivated to perform in the best interests of shareholders, commensurate with ensuring shareholder value.</p>
<p>The Committee seeks to ensure that executive Directors’ remuneration is aligned with shareholders’ interests and the Group’s strategy. Share awards are therefore seen as the principal method of long-term incentivisation. Executive Directors are incentivised on a range of equity share structures, notably the significant share ownership held by Stephen Glancey and Kenny Neison through the Joint Share Ownership Plan. Similar principles are applied for senior management, several of whom have material equity holdings in the Company.</p>
<p>Annual performance-related rewards aligned with the Group’s key financial, operational and strategic goals and based on stretching targets are a further component of the total executive remuneration package. For senior management, mechanisms are tailored to local requirements.</p>
<p>The Group seeks to bring transparency to executive Directors’ reward structures through the use of cash allowances in place of benefits in kind. In setting executive Directors’ remuneration the Committee has regard to pay levels and conditions applicable to other employees across the Group.</p>
