<h3>Malus and clawback</h3>
<p>In line with the recently updated UK Corporate Governance Code malus and clawback provisions will apply to all elements of performance-based variable remuneration (i.e. annual bonus, ESOS 2015 and LTIP 2015) for the executive Directors with effect from 1 March 2016. The circumstances in which malus and clawback will be applied are if there has been in the opinion of the Committee a material mis-statement of the Group’s published accounts; or the Committee reasonably determines that a participant has been guilty of gross misconduct. The clawback provisions will apply for a period of two years following the end of the performance period.</p>
<!---->
<h3>Payments under the LTIP (Part I)</h3>
<p>At the 2015 AGM, shareholders are being asked to approve a resolution extending the exercise period of the LTIP (Part I). Currently the rules of the Plan are drafted such that executives have a six month window in which to exercise options vesting under the LTIP (Part I). If the executives are prohibited from exercising their options because the Company is in a close period, then the options will lapse. This is not the intention of the Plan, which should allow for the exercise period to be extended in this situation and we are therefore seeking to amend the rules to provide this flexibility by extending this window from six months to three years from vesting.</p>
<!---->
<h2>Discretion to depart from policy</h2>
<h3>Share schemes and other incentives</h3>
<p>The Committee recognises the importance of ensuring that the outcomes of the Group’s executive pay arrangements properly reflect the Group’s overall performance over the performance period. It is the Committee’s intention that the mechanistic application of performance conditions relating to awards will routinely be reviewed to avoid outcomes which could be seen as contrary to shareholders’ expectations.</p>
<p>To the extent provided for in accordance with any relevant amendment power under the rules of the share plans or in the terms of any performance condition, the Committee may alter the performance conditions relating to an award or option already granted if an event occurs (such as a material acquisition or divestment or unexpected event) which the Committee reasonably considers means that the performance conditions would not, without alteration, achieve their original purpose. The Committee will act fairly and reasonably in making the alteration so that the performance conditions achieve their original purpose and the thresholds remain as challenging as originally imposed. The Committee will explain and disclose any such alteration in the next remuneration report.</p>
<!---->
<h3>Legacy payments</h3>
<p>The Committee reserves the right to make any remuneration payment or any payment for loss of office without the need to consult with shareholders or seek their approval, notwithstanding that it is not in line with the policy set out above, where the terms of the payment were agreed either:</p>
<ul class="list">
	<li>before the policy came into effect; or</li>
	<li>at a time when the relevant individual was not a Director of the Company and, in the opinion of the Committee, the payment was not in consideration for the individual becoming a Director of the Company.</li>
</ul>
<p>For these purposes: the term ‘payment’ includes any award of variable remuneration; in relation to an award over shares, the terms of the payment are ‘agreed’ at the time the award is granted.</p>
