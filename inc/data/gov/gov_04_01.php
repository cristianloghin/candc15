<p class="large">Directors’ Remuneration Policy</p>
<p>This part of the report sets out the Group’s policy on Directors’ remuneration. The policy has been determined by the Committee of the Board of Directors (“the Committee”). The Directors’ remuneration policy will be subject to an advisory vote at the 2015 AGM and will take effect from that date.</p>
