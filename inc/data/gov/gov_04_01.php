<h2>Comparison with remuneration policy for employees generally</h2>
<p>Remuneration packages for executive Directors and for employees as a whole reflect the same general remuneration principle that individuals should be rewarded on their contribution to the Group and its success, and the reward they receive should be competitive in the market in which they operate without paying more than is necessary to recruit and retain them.</p>
<p>The remuneration package for executive Directors reflects their role of leading the strategic development of the Group. Accordingly there is a strong alignment with shareholders’ interests, through long term performance-based share rewards. Senior management are similarly rewarded.</p>
<p>These rewards are not appropriate for all employees but it is the Committee’s policy that employees in general should be afforded an opportunity to participate in the Group’s success through holding shares in the Company through all-employee schemes.</p>
<p>Executive Directors are incentivised through an annual cash bonus to achieve shorter term objectives and all employees are similarly incentivised.</p>
<p>For executive Directors the remuneration package reflects the demands of a global market. For employees generally remuneration and reward are tailored to the local market in which they work. It is the Committee’s policy that all employees should share in the success of the business divisions towards whose success they have contributed.</p>
