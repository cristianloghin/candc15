	<h2>FY2015 key decisions and incentive out-turn</h2>
	<p>The Board remain committed to a responsible approach to executive pay. During the year ended 28 February 2015 the Committee reviewed executive remuneration, however the executive Directors did not consider it appropriate to take a salary increase in FY2015 and this was supported by the Committee. The Committee feels it is important to observe that the Chief Executive Officer and Chief Finance Officer have not received a salary increase since November 2008 having waived their contractual entitlement to indexation increases in 2009 and 2010. They waived bonus payments in 2009 and in FY2014 they also waived share awards in order to facilitate larger awards to be made to a wider audience. Similarly no increase was made in the year to non-executive Director fees nor has any increase been made to these fees since 2008.</p>
	<p>As set out on further down, no bonus was paid to the executive Directors for FY2015. The Committee further determined that in respect of awards made in FY2012 under the C&amp;C Executive Share Option Scheme (ESOS) and the C&amp;C Long Term Incentive Plan (Part I) (LTIP (Part I)), the performance conditions were not met and the awards did not vest.</p>
	<p>In addition the Committee approved various share awards and the principles of the FY2016 bonus scheme.</p>
	<!---->
	<h2>FY2016 arrangements</h2>
	<p>The Committee considered a salary increase for the executive Directors for FY2016 but the executive Directors, as part of their on-going commitment to, and investment in the business, did not consider it appropriate to take an increase, which was supported by the Committee. Variable pay awards will be unchanged from the prior year with the same overall maximum awards under the ESOS and LTIP (Part I) and, except for awards to be made under the ESOS, the same performance conditions attaching as in FY2015. For the ESOS awards we will apply a vesting schedule with straight line vesting between a threshold EPS target of 3% and a maximum EPS target of 6%, replacing the cliff vesting of prior years.</p>
	<p>Shareholders are also being asked to approve the extension of the exercise period of the LTIP (Part I). Currently the rules of this Scheme are drafted such that executives have a six month window in which to exercise options vesting under the LTIP (Part I). If the executives are prohibited from exercising their options because the Company is in a close period, then the options will lapse. This is not the intention of the Scheme which should allow for the exercise period to be extended in this situation and we are therefore seeking to amend the rules to provide this flexibility by extending this window from six months to three years from vesting.</p>
