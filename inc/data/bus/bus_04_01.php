<h3>Commodity price and other risk management</h3>
<p>The Group is exposed to commodity price fluctuations, and manages this risk, where economically viable, by entering into fixed price supply contracts with suppliers. The Group does not directly enter into commodity hedge contracts. The cost of production is also sensitive to variability in the price of energy, primarily gas and electricity. It is Group policy to fix the cost of a certain level of its energy requirement through fixed price contractual arrangements directly with its energy suppliers.</p>
<p>The Group seeks to mitigate risks in relation to the continuity of supply of key raw materials and ingredients by developing trade relationships with key suppliers. The Group has over 60 long-term apple supply contracts with farmers in the west of England and has an agreement with malt farmers in Scotland for the supply of barley.</p>
<p>In addition, the Group enters into insurance arrangements to cover certain insurable risks where external insurance is considered by management to be an economic means of mitigating these risks.</p>
