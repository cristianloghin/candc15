<h3>Results for the Year</h3>
<p class="large">C&amp;C is reporting net revenue of €683.9 million (up 10.3%), <span class="note">operating profit<span>Operating profit is before exceptional items.</span></span> of €115 million (down 9.2%) and <span class="note">adjusted diluted EPS<span>Adjusted basic/diluted earnings per share (‘EPS’) is before exceptional items. Please see Note 9 to the financial statements.</span></span> of 27.2 cent (down 7.8%).</p>
<!---->
<p>On a <span class="note">constant currency basis<span>Constant currency calculation is set out at the bottom of this page.</span></span>, the net revenue is up 6.6% primarily as a result of the consolidation of the current year acquisition of Wallaces Express in the Group’s numbers while operating profit(i) for the year was down 11.2%. Excluding Wallaces Express, net revenue and <span class="note">operating profits<span>Operating profit is before exceptional items.</span></span> are down 7.7% and 12.6% respectively on a constant currency basis.</p>
<p>The results for the year were disappointing, primarily driven by the performance in C&amp;C Brands and the US. This resulted in the Group failing to achieve its guidance targets communicated last July. However, the Group did achieve guidance communicated in January for both operating profit and free cashflow conversion.</p>
<p>In C&amp;C Brands, intense competition between off-trade retailers coupled with continuing brand proliferation, has led to significant pressure on both volumes and pricing, highlighting the need for structural change to the fixed cost base. Changes in this area are well advanced.</p>
<p>In the US, the disruptive element of new market entrants, attracted by the category growth resulted in volume decline in a business where we are investing in infrastructure. This has resulted in profit erosion. The destabilising impact of new entrants is reducing and we are now better positioned to share in the continued dynamic growth of the category. As a result of rebasing our profit expectations of the business we have reassessed the value of our intangible assets in the US and have taken an impairment charge of €150 million as at year end date. This non-cash adjustment has been treated as an exceptional cost.</p>
<p>In Ireland and Scotland the focus remains on building our brand led wholesale model. We completed the acquisition of Wallaces Express during the year and integration and restructuring of that business with the Group’s existing business in Scotland has progressed well. We believe we have made significant progress and expect the integrated model to be in place in Scotland by summer 2015. We believe the branded wholesale model is the right business model for stable mature profitable territories such as Ireland and Scotland.</p>
<p>Currency has been positive for us in FY2015 and helped offset some of the decline in C&amp;C Brands and the US. Cash generation has improved on last year and the business remains conservatively geared. This balance sheet strength allowed us to invest €30 million in a share buy-back programme in the latter half of the year, purchasing 9,025,000 shares at an average share price of €3.29. All shares acquired as part of the share buy-back programme are held as treasury shares.</p>
