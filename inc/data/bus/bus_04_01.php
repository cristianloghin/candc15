<h2>Balance Sheet Strength, Debt Management and Cashflow Generation</h2>
<p>Balance sheet strength provides the Group with the financial flexibility to pursue its strategic objectives. It is Group policy to ensure that a medium/long term debt funding structure is in place to provide the Group with the financial capacity to promote the future development of the business and to achieve its strategic objectives.</p>
<p>During the current financial year the Group amended and updated its committed €450 million multi-currency five year syndicated revolving loan facility. The facility agreement provides for a further €100 million in the form of an uncommitted accordion facility and permits the Group to have additional indebtedness to a maximum of €150 million, giving the Group debt capacity of €700 million. The debt facility matures on 22 December 2019. At 28 February 2015 <span class="note">net debt<span>Net debt comprises borrowings (net of issue costs) less cash &amp; cash equivalents.</span></span> was €157.8 million representing a <span class="note">net debt:EBITDA<span>EBITDA is earnings before exceptional items, finance income, finance expense, tax, depreciation, amortisation charges and Equity accounted investees’ (loss)/profit after tax.</span></span> ratio of 1.1x.</p>
<p>Brand values and goodwill are assessed for impairment on an annual basis by comparing the carrying value of the assets with their recoverable amounts using value in use computations. As outlined above an impairment with respect to the US business was taken in the current financial year. All other business segments had sufficient headroom. No reasonable movement in any of the underlying assumptions would result in an impairment in the Ireland, Scotland, C&amp;C Brands or Export business segments.</p>
