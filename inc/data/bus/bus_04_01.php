<h2>Financial Risk Management</h2>
<p>The most significant financial market risks facing the Group continue to include foreign currency exchange rate risk, commodity price fluctuations, interest rate risk and creditworthiness risk in relation to its counterparties.</p>
<p>The Board of Directors set the treasury policies and objectives of the Group, the implementation of which is monitored by the Audit Committee. There has been no significant change during the financial year to the Board’s approach to the management of these risks. Details of both the policies and control procedures adopted to manage these financial risks are set out in detail in note 22 to the financial statements.</p>
<h3>Currency risk management</h3>
<p>The Group’s reporting currency and the currency used for all planning and budgetary purposes is the euro. However, as the Group transacts in foreign currencies and consolidates the results of non-euro reporting foreign operations, it is exposed to both transaction and translation currency risk.</p>
<p>Currency transaction exposures primarily arise on the sterling, US, Canadian and Australian dollar denominated sales of the Group’s euro subsidiaries. The Group seeks to minimise this exposure, when economically viable to do so, by maximising the value of its foreign currency input costs and creating a natural hedge. When the remaining net exposure is material, the Group manages it by hedging an appropriate portion for a period of up to two years ahead. Forward foreign currency contracts are used to manage this risk in a non-speculative manner. The Group had no outstanding forward foreign currency contracts as at the year-end date.</p>
<p>The effective rate for the translation of results from sterling currency operations was €1:£0.795 (year ended 28 February 2014: €1:£0.846) and from US dollar operations was €1:$1.295 (year ended 28 February 2014: €1:$1.334).</p>
<p>Comparisons for revenue, net revenue and operating profit for each of the Group’s reporting segments are shown at constant exchange rates for transactions by subsidiary undertakings in currencies other than their functional currency and for translation in relation to the Group’s sterling and US dollar denominated subsidiaries by restating the prior year at FY2015 effective rates.</p>
