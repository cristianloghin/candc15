<h3>Exceptional items</h3>
<p>FY2015 represented a year of revaluation, restructuring, integration and consolidation. Consequently costs of €173.4 million were incurred, which due to their nature and materiality were classified as exceptional items for reporting purposes, a presentation which, in the opinion of the Board, provides a more helpful analysis of the underlying performance of the Group.</p>
<p>The main items which were classified as exceptional include:-</p>
<p>(a) Impairment of intangible assets: Brand values and goodwill are assessed for impairment on an annual basis by comparing the carrying value of the assets with their recoverable amounts using value in use computations. A review of the carrying value of all intangible asset values was completed during FY2015 and as a result of this review an impairment charge of €150 million was taken with respect of the Group’s intangible assets in the US. This represented a write down of 19% of the Group’s intangible assets. The magnitude of the impairment charge is increased due to the strengthening of the dollar against the euro in the current financial year; a comparable impairment last year would have resulted in a charge of €122.7 million.</p>
<p>(b) Restructuring costs: Restructuring costs of €2.8 million comprising severance and other initiatives primarily arose from a reorganisation programme in England &amp; Wales.</p>
<p>(c) Acquisition related costs: The Group completed the acquisition of Green Light Brands Ltd., Monuriki Drinks Ltd., and Monuriki Sales and Marketing Ltd. during the current financial year for €3.2 million. These were external consultancy entities that provided sales and marketing services to the Group’s Shepton Mallet Cider Mill business and its International Wines &amp; Spirits division. A decision was taken to bring these entities in-house as part of a rationalisation initiative of the Group’s sales and marketing structure. The Group also incurred costs of €0.5 million with respect of the Group’s preliminary approach for the Spirit Pub Group.</p>
<p>(d) Revaluation of property, plant &amp; equipment: In line with Group policy, an external valuation of the Group’s property plant &amp; equipment was completed in FY2015. This resulted in a net revaluation loss of €10.5 million accounted for in the income statement and a gain of €5.3 million which was accounted for within other comprehensive income. Also during the year, in light of a material reduction in the utilisation levels of a bottling line located at the Group’s Shepton Mallet cider manufacturing plant, a decision was taken to impair the bottling line by €3.3 million.</p>
<p>(e) Impairment of investment in equity accounted investee: The Group impaired its investment in the Maclay Group plc as a result of the Maclay Group plc entering administration proceedings during the financial year. This resulted in an impairment of the Group’s investment of €2.0 million and the impairment of related derivative financial instruments of €0.6 million which were accounted for within finance expense.</p>
<p>(f) Integration costs: Integration costs of €2.2 million were incurred in the financial year comprising professional and other related fees primarily attributed to the integration of the acquired Wallaces Express and Gleeson businesses with the Group’s existing business.</p>
<p>(g) Profit on disposal of property, plant &amp; equipment: A profit of €0.8 million was realised following the disposal of land &amp; buildings which were surplus to requirements.</p>
