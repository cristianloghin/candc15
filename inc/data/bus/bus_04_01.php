<p>The income tax charge in the year excluding the charge in relation to exceptional items and equity accounted investees amounted to €14.6 million. This represents an effective tax rate of 13.7%, an increase of 0.6 percentage points on the prior year. The Group has more profits taxed in the UK in the current year following the acquisition of Wallaces Express which has resulted in this increase year on year. The effective tax rate of 13.7% reflects the fact that the Group is established in Ireland’s low tax environment, allowing the Group to avail of the 12.5% tax rate on profits generated in Ireland.</p>
