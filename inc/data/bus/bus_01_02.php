			<h4>United States</h4>
			<p>The cider category has experienced excellent growth with volume of 25.6m cases in calendar year 2014, up 54% on the <span class="note">previous year<span>Per Nielsen/CGA</span></span>. For our cider business, however, the year was defined by severe market disruption as global and domestic brewers invested heavily to build a distribution footprint for their new brands. At the same time, a small but growing local craft cider movement has become a market feature. As a consequence our share of the category has come under pressure and Woodchuck brand depletions were down 15%.</p>
			<p>During the year we have continued to invest in the US business. The new state of the art $34.5m cidery In Vermont was completed and is now in operation. The Woodchuck brand has been refreshed with new packaging and a new marketing campaign with activity focussed on selected States. The Group have also launched a number of innovative Woodchuck line extensions. The two most recent launches are Hopsation and Gumption and both have received positive feedback from distributors and consumers. In the last 6 months the Group has seen Woodchuck share of the key off-trade channel stabilise in the month on month <span class="note">retail data<span>Per Nielsen/CGA</span></span>. In addition, the distributor network has been stable and supportive during FY2015. There are encouraging signs as we move into the FY2016.</p>
		</div>
		<div>
			<p>Shipments of the Magners brand grew by 2% despite competitive pressure from new entrants. The brand now enjoys wider distribution across the United States which has helped deliver the volume growth. Blackthorn shipment volumes grew by 29% with a dry authentic English cider offering something different to the consumer. The Hornsby brand continued to decline in the year as we focus on other brands in the portfolio.</p>
			<p>Despite the numbers of new entrants to the category, the retail pricing environment has remained relatively stable with a price point similar to high end craft beer and well above the beer category average.</p>
			<p>The business continued to invest in sales and marketing activities despite lower volumes with the combined cost now 27% of sales. At the same time, factory utilisation deteriorated due to the cessation of a major packaging contract towards the end of FY2014. The combined effect of volume decline, loss of contract packaging and increased investment in sales/marketing was significant downward pressure on operating profit. The US asset is well invested, however, and as the market stabilises any uplift in volume will flow through to bottom line profitability.</p>
