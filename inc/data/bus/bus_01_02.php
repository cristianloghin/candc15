	 		<h4>Gleeson</h4>
	 		<p>The Gleeson business has had a mixed year. Integration has significantly changed the business model in Ireland. The Irish business now services customers with one Island of Ireland sales force, from a new information technology platform and we are in the process of setting up a central telesales operation. Support functions have been consolidated and the restructured sales, marketing and finance functions have delivered cost savings in the year. There has been some pressure on Gleeson revenues as a consequence of gains and losses in distribution contracts. A number of brand owners made the decision earlier in the year to move to alternative distributors for competitive reasons. In the latter part of the year we won a number of new high quality contracts, most notably distribution rights for Corona lager in Ireland and a sizeable wine supply deal. Inevitably, there will be some volatility in revenue as old arrangements cease and new ones commence and the business continues to develop the operating model to mitigate the impact of revenue volatility.</p>
