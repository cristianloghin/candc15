<h2>Corporate Responsibility</h2>
<p class="intro">We are supporting the implementation of minimum unit pricing in Scotland, the Republic of Ireland and Northern Ireland.</p>
<p class="intro">We are campaigning for the changes in the tied pub laws that have been introduced in England and Wales to be applied in Scotland.</p>
<p class="intro">We are working with governmental bodies, Drinkaware and police forces on initiatives to improve the safety of the night time economy.</p>
<p class="intro">We are working to encourage the adoption of a new Cider Bill to help improve the quality of cider products in the US.</p>
<p class="intro">The Tennent’s Training Academy continues to provide high quality hospitality industry training, now having trained over 16,000 people.</p>
<p class="intro">We have made significant charitable contributions at the local and national level.</p>
<p class="intro">We have invested €1 million at Wellpark to deliver energy efficiencies and reduce GHG emissions.</p>
<p class="intro">Our main production sites have reduced energy consumption by 12% compared to three years ago.</p>
<p class="intro">Health and Safety programmes have delivered a significant reduction in the number of injuries resulting in lost-work days.</p>
<p class="intro">We pay corporation tax in the main geographies which we operate in and pay substantial amounts of alcohol duty.</p>

