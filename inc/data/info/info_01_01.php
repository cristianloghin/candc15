<h2>DIVIDEND PAYMENTS</h2>
<p>The Company may, by ordinary resolution declare dividends in accordance with the respective rights of shareholders, but no dividend shall exceed the amount recommended by the Directors. The Directors may also declare and pay interim dividends if they believe they are justified by the profits of the Company available for distribution.</p>
<p>An interim dividend of 4.5 cent per share was paid in respect of ordinary shares on 23 December 2014.</p>
<p>A final dividend of 7.0 cent, if approved by shareholders at the 2015 Annual General Meeting, will be paid in respect of ordinary shares on 10 July 2015 to shareholders on the record on 22 May 2015. A scrip alternative will be offered to shareholders.</p>
<p>Dividend Withholding Tax (‘DWT’) must be deducted from dividends paid by an Irish resident company, unless a shareholder is entitled to an exemption and has submitted a properly completed exemption form to the Company’s Registrars. DWT applies to dividends paid by way of cash or by way of shares under a scrip dividend scheme and is deducted at the standard rate of income tax (currently 20%). Non-resident shareholders and certain Irish companies, trusts, pension schemes, investment undertakings, companies resident in any member state of the European Union and charities may be entitled to claim exemption from DWT and have been sent the relevant exemption form. Further copies of the form may be obtained from the Company’s Registrars. Shareholders should note that DWT will be deducted from dividends in cases where a properly completed exemption form has not been received by the relevant record date. Shareholders who wish to have their dividend paid direct to a bank account, by electronic funds transfer, should contact Capita Registrars to obtain a mandate form. Tax vouchers will be sent to the shareholder’s registered address under this arrangement.</p>
<h4>CREST members</h4>
<p>Shareholders who hold their shares via CREST will automatically receive dividends in euro unless they elect otherwise.</p>
<h4>Non-CREST members</h4>
<p>Shareholders who hold their shares in certificate form will automatically receive dividends in euro with the following exceptions:</p>
<ul class="list">
	<li>Shareholders with an address in the United Kingdom (UK) will automatically receive dividends in sterling,</li>
	<li>Shareholders who had previously elected to receive dividends in a particular currency will continue to receive dividends in that currency.</li>
</ul>
<p>Shareholders who wish to receive dividends in a currency other than that which will be automatically used should contact the Company’s Registrars.</p>
