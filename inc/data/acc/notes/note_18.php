<h4>Borrowing facilities</h4>
<p>The Group manages its borrowing requirements by entering into committed loan facility agreements.</p>
<p>In December 2014, the Group amended and updated its committed €450m multi-currency five year syndicated revolving loan facility with seven banks, namely Bank of Ireland, Bank of Scotland, Barclays Bank, Danske Bank, HSBC, Rabobank, and Ulster Bank, repayable in a single instalment on 22 December 2019. The facility agreement provides for a further €100m in the form of an uncommitted accordion facility and permits the Group to avail of further financial indebtedness, excluding working capital and guarantee facilities, to a maximum value of €150m, subject to agreeing the terms and conditions with the lenders. Consequently the Group is permitted under the terms of the agreement, to have debt capacity of €700m of which €342.8m was drawn at 28 February 2015 (2014: €309.6m was drawn under the Group’s 2012 multi-currency facility). This 5 year multi-currency facility replaces the Group’s previous multi-currency facility which was negotiated in February 2012 and which was due to mature in February 2017. Balances outstanding under the 2012 facility were deemed to have been repaid as part of the December 2014 refinancing with amounts simultaneously re-drawn under the amended facility.</p>
<p>Under the terms of the agreement, the Group must pay a commitment fee based on 40% of the applicable margin on undrawn committed amounts and variable interest on drawn amounts based on variable Euribor/Libor interest rates plus a margin, the level of which is dependent on the net debt:EBITDA ratio, plus a utilisation fee, the level of which is dependent on percentage utilisation. The Group may select an interest period of one, two, three or six months.</p>
<p>All non-current bank loans are guaranteed by a number of the Group’s subsidiary undertakings. The facility agreement allows the early repayment of debt without incurring additional charges or penalties. All non current bank loans are repayable in full on change of control of the Group.</p>
<p>The Group’s multi-currency debt facility incorporates two financial covenants:</p>
<ul class="list">
	<li>Interest cover: The ratio of EBITDA to net interest for a period of 12 months ending on each half year date will not be less than 3.5:1</li>
	<li>Net debt/EBITDA: The ratio of net debt on each half year date to EBITDA for a period of 12 months ending on a half year date will not exceed 3.5:1</li>
</ul>
<p>The Group complied with both covenants throughout the current and prior financial year.</p>
<p>Further information about the Group’s exposure to interest rate, foreign currency and liquidity risk is disclosed in note 22.</p>
<h4>Debt on acquisition</h4>
<p>During the prior financial year, the Group acquired debt of €3.6m on the acquisition of Biofun, of which €3.5m was repaid during the prior financial year with the remaining outstanding debt as at 28 February 2014 of €0.1m classified within current liabilities. This outstanding debt was fully repaid and cancelled on 21 March 2014.</p>