<h4>Restructuring</h4>
<p>The closing restructuring provision and current year charge primarily relate to severance costs arising from a reorganisation programme in England &amp; Wales. The prior year closing restructuring provision and the amount utilised in the current financial year primarily relate to the Group’s reorganisation programme in Ireland following the prior year acquisition of Gleeson. The provision is expected to be fully utilised in the next financial year. </p>
<h4>Onerous leases</h4>
<p>The onerous lease provision relates to two onerous leases in relation to warehousing facilities acquired as part of the acquisition of the Gaymers cider business in 2010. These onerous leases expire in 2017 and 2026 respectively. The Group also had an onerous lease, which expired during the prior financial year, in relation to the consolidation of the Group’s Dublin offices into a single location in 2009. This resulted in a release of €0.3m to the income statement in the prior financial year (note 5).</p>
<h4>Other</h4>
<p>Other provisions relate to a provision for the Group’s exposure to employee and third party insurance claims. Under the terms of employer and public liability insurance policies, the Group bears a portion of the cost of each claim up to the specified excess. The provision is calculated based on the expected portion of settlement costs to be borne by the Group in respect of specific claims arising before the balance sheet date. </p>