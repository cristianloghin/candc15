<p>The Group has not recognised deferred tax in relation to temporary differences applicable to investments in subsidiaries on the basis that the Group can control the timing and the realisation of these temporary differences and it is unlikely that the temporary differences will reverse in the foreseeable future. The aggregate amount of temporary differences applicable to investments in subsidiaries and equity accounted investees in respect of which deferred tax liabilities have not been recognised is immaterial on the basis that the participation exemptions and foreign tax credits should be available such that no material temporary differences arise. There are no other unrecognised deferred tax liabilities.</p>
<p>In addition, no deferred tax asset has been recognised in respect of certain tax losses incurred by the Group on the basis that the recovery is considered unlikely in the foreseeable future. The value of such tax losses is €5.5m in the current financial year (2014: €1.7m). In the event that sufficient taxable profits arise in the relevant jurisdictions in future years, these losses may be utilised. The vast majority of these losses are due to expire in 2035.</p>
