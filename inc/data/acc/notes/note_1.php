<h2>1. segmental reporting</h2>
<p>The Group’s business activity is the manufacturing, marketing and distribution of alcoholic drinks and five operating segments have been identified in the current period; Ireland, Scotland, C&amp;C Brands, North America and Export.</p>
<p>The Group continually reviews and updates the manner in which it monitors and controls its financial operations resulting in changes in the manner in which information is classified and reported to the Chief Operating Decision Maker (“CODM”). The CODM, identified as the executive Directors comprising Stephen Glancey, Kenny Neison and Joris Brams, assesses and monitors the operating results of segments separately via internal management reports in order to effectively manage the business and allocate resources.</p>
<p>Following the acquisition of the Gleeson and Wallaces Express wholesaling businesses in Ireland and Scotland respectively and subsequent restructuring of the Group’s business, the basis of segmentation was amended during the current financial year to reflect the new business model. The revised basis of segmentation is outlined in the paragraphs below but in all instances the changes were deemed necessary to better enable the CODM to evaluate the results of the business in the context of the economic environment in which the business operates, to make appropriate strategic decisions and to more accurately reflect the business model under which the Group now operates in each of these territories. All comparative amounts have been restated to reflect the new basis of segmentation. The reclassification has no impact on Revenue, Net revenue or Operating profit reported by the Group.</p>
<p>The identified reporting segments are as follows:-</p>
<h4>(i) Ireland</h4>
<p>This segment includes the financial results from sale of own branded products in the Island of Ireland, principally Bulmers, Tennent’s, Magners, Clonmel 1650, Heverlee, Caledonia Smooth, Finches and Tipperary Water. It also includes the financial results from beer and wines &amp; spirits distribution and wholesaling following the acquisition of Gleeson, and the results from sale of third party brands as permitted under the terms of a distribution agreement with AB InBev.</p>
<p>The Northern Ireland business, previously reported within the Cider UK, Tennent’s UK and Third Party Brands UK segments, is now included within this new segment following the consolidation of this business with the Republic of Ireland business, the appointment of an Island of Ireland Managing Director supported by a single management team and the completion of the integration of a number of key functions including sales, marketing and accounting services.</p>
<h4>(ii) Scotland</h4>
<p>This segment includes the results from sale of the Group’s own branded beer brands in Scotland, with Tennent’s, Heverlee, Caledonia Best and Magners the principal brands. It also includes the financial results from third party brand distribution and wholesaling in Scotland following the current year acquisition of the Wallaces Express wholesale business. Both the existing Scottish business and the acquired Wallaces Express business are controlled and managed under one Managing Director and management team and key functions such as sales, marketing and accounting services are in the process of being integrated.</p>
<h4>(iii) C&amp;C Brands</h4>
<p>This segment includes the results from sale of the Group’s own branded products in England &amp; Wales, principally Magners, Tennent’s, Chaplin &amp; Cork’s and K Cider. It also includes the distribution of the Italian lager Menabrea and the production and distribution of private label cider products in England &amp; Wales. The consolidated C&amp;C Brands business is managed by one Managing Director and management team. (This segment was previously called England &amp; Wales for the period ended 31 August 2014).</p>
<h4>(iv) North America</h4>
<p>This segment includes the results from sale of the Group’s cider and beer products, principally Woodchuck, Magners, Blackthorn, Hornsby’s and Tennent’s in the United States of America and Canada. Following the acquisition of the Vermont Hard Cider business and the consequential decision to manage and control this business independently from the Group’s Export division, this business is now reviewed and strategically managed by the CODM as a separate business unit.</p>
<h4>(v) Export</h4>
<p>This segment includes the sale and distribution of the Group’s own branded products, principally Magners, Gaymers, Blackthorn, Hornsby’s and Tennent’s outside of Ireland, Scotland, England &amp; Wales and North America. It also includes the sale of some third party brands.</p>
<p>The analysis by segment includes both items directly attributable to a segment and those, including central overheads, which are allocated on a reasonable basis in presenting information to the CODM.</p>
<p>Inter-segmental revenue is not material and thus not subject to separate disclosure.</p>
