<h2>10. BUSINESS COMBINATIONS</h2>
<h3>Acquisition of businesses</h3>
<p>During the current financial year, on 18 March 2014, the Group announced the acquisition of the remaining 50% equity share capital of Wallaces Express Limited (“Wallaces Express”), a wholesaler of beverages in Scotland. This purchase follows the acquisition of a 50% stake in the business in March 2013. </p>
<p>The Group completed the acquisition of Green Light Brands Ltd., Monuriki Drinks Ltd., and Monuriki Sales and Marketing Ltd. (collectively referred to as “Green Light Brands”) during the current financial year, on 19 January 2015, for €3.2m. Green Light Brands was an external consultancy entity that provided sales and marketing services to the Group’s Shepton Mallet Cider Mill Brands while Monuriki had provided similar support to the Group’s International Wines and Spirits business. A decision was taken bring these entities in-house as part of a rationalisation initiative of the Group’s sales and marketing structure.</p>
<p>Also during the current financial year, the Group finalised its assessment of the fair value of assets and liabilities acquired as part of the acquisition of Biofun Produtos Biológicos do Fundão, Lda (“Biofun”), a producer and seller of fruit concentrates based in the district of Castelo Branco, Portugal, which the Group acquired on 2 August 2013.</p>
<p>In the prior financial year, the Group completed the acquisition of M. &amp; J. Gleeson (Investments) Limited (“Gleeson”) and its subsidiaries, a supplier and distributor of beverages in Ireland, on 7 March 2013.</p>
