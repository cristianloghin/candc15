<h4>Deemed disposal of equity accounted investee – initial investment in Wallaces Express Limited</h4>
<p>On 18 March 2014, the Group announced the acquisition of the remaining 50% equity share capital of Wallaces Express. Under IAS 28 Investments in Associates and Joint Ventures, this necessitated the deemed disposal of the Group’s initial 50% investment which was classified as an equity accounted investee and the recognition of the acquisition of control of the business under IFRS 3 Business Combinations.</p>
<p>The Group’s share of profits recognised in the period from initial acquisition of the equity accounted investee, on 22 March 2013, to date of deemed disposal on 18 March 2014 was €0.6m. In addition, the Group had recognised €0.15m in the foreign currency reserve which was recycled to the income statement in the current period following this deemed disposal.</p>		