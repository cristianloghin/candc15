<h3>Impairment testing</h3>
<p>To ensure that goodwill and brands considered to have an indefinite useful economic life are not carried at above their recoverable amount, impairment reviews are performed comparing the carrying value of the assets with their recoverable amount using value-in-use computations. Impairment testing is performed annually or more frequently if there is an indication that the carrying amount may not be recoverable. Where the value in use exceeds the carrying value of the asset, the asset is not impaired.</p>
<p>As permitted by IAS 36 Impairment of Assets, the value of the Group’s intangible assets (goodwill and brands) has been allocated to groups of cash generating units (referred to in this note as a business segment), which are not larger than an operating segment determined in accordance with IFRS 8 Operating Segments. These business segments represent the lowest levels within the Group at which the associated goodwill and indefinite life brands are monitored for management purposes.</p>
<p>The recoverable amount is calculated in respect of each business segment using value-in-use computations based on estimated future cash flows discounted to present value using a discount rate appropriate to each cash generating unit and terminal values calculated on the assumption that cash flows continue in perpetuity.</p>
<p>The key assumptions used in the value-in-use computations are:-</p>
<ul class="list">
	<li>Expected volume, net revenue and operating profit growth rates - cash flows for each business segment are based on detailed financial budgets and plans, formally approved by the Board, for years one to three; these cash flows are extrapolated out for years four and five;</li>
	<li>Long term growth rate - cash flows after the first five years were extrapolated using a long term growth rate, on the assumption that cash flows for the first five years will increase at a nominal growth rate in perpetuity,</li>
	<li>Discount rate.</li>
</ul>
<p>The key assumptions were based on management assessment of anticipated market conditions for each business segment. A terminal growth rate of between 2.0%-2.5% (2014: 2.5%-3.0%) in perpetuity was assumed based on an assessment of the likely long term growth prospects for the sectors and geographies in which the Group operates. The resulting cash flows were discounted to present value using a range of discount rates between 8%-10% (2014: 8%-10%); these rates are in line with the Group’s estimated pre-tax weighted average cost of capital for the three main geographies in which the Group operates (Ireland, Great Britain and North America), arrived at using the Capital Asset Pricing Model.</p>
<p>In formulating the budget and three year plan the Group takes into account historical experience, an appreciation of its core strengths and weaknesses in the markets in which it operates and external factors such as macro economic factors, inflation expectations by geography, regulation and expected changes in regulation (such as expected changes to duty rates and minimum pricing), market growth rates, sales price trend, competitor activity, market share targets and strategic plans and initiatives.</p>
