<p>The impairment testing carried out at 28 February 2015 identified headroom in the recoverable amount of the brands and goodwill compared to their carrying values in all business segments excluding North America. The testing identified an impairment charge in North America of €150.0m. The carrying value of the North America segment is the Directors’ estimate of its recoverable amount after the impairment charge. Any variation to the input assumptions would result in a further impairment charge.</p>
<p>The key sensitivities for the impairment testing are net revenue and operating profit growth assumptions, discount rates applied to the resulting cashflows and the expected long term growth rates. The impairment testing carried out at 28 February 2015 identified headroom in the recoverable amount of the brands and goodwill compared to their carrying values in all business segments.</p>
<p>The value in use calculations indicate significant headroom in respect of the Ireland and Scotland operating segments. In the case of C&amp;C Brands, the level of headroom, while significantly less than the headroom in the Ireland and Scotland operating segments, is in excess of €50.0m. No reasonable movement in any of the underlying assumptions would result in an impairment in the Ireland, Scotland, C&amp;C Brands or Export business segments.</p>