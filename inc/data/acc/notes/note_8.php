<p>The Directors have proposed a final dividend of 7.0 cent per share (2014: 5.7 cent), to ordinary shareholders registered at the close of business on 22 May 2015, which is subject to shareholder approval at the Annual General Meeting, giving a proposed total dividend for the year of 11.5 cent per share (2014: 10.0 cent). Using the number of shares in issue at 28 February 2015 and excluding those shares for which it is assumed that the right to dividend will be waived, this would equate to a distribution of €23.6m (2014: €19.6m).</p>
<p>In order to achieve better alignment of the interest of share based remuneration award recipients with the interests of shareholders, shareholder approval was given at the 2012 AGM to a proposal that awards made in or after 2012 and that vest under the LTIP (Part I) incentive programme should reflect the equivalent value to that which accrues to shareholders by way of dividends during the vesting period. The current year charge for dividends of €35.1m is net of the release of an accrual of €0.1m with respect to LTIP (Part I) dividend entitlements which were accrued in previous years but for which the related LTIP (Part I) award was deemed to have lapsed in the current financial year and hence the related dividend entitlement lapsed. The prior year included a charge of €0.1million with respect to an accrual for LTIP (Part I) dividend entitlements.</p>
<p>Total dividends of 10.2 cent per ordinary share were recognised as a deduction from the retained income reserve in the year ended 28 February 2015 (2014: 9.05 cent).</p>
<p>Final dividends on ordinary shares are recognised as a liability in the financial statements only after they have been approved at an annual general meeting of the Company. Interim dividends on ordinary shares are recognised when they are paid.</p>		