<h2>26. RELATED PARTY TRANSACTIONS</h2>
<p>The principal related party relationships requiring disclosure in the consolidated financial statements of the Group under IAS 24 Related Party Disclosures pertain to the existence of subsidiary undertakings and equity accounted investees, transactions entered into by the Group with these subsidiary undertakings and equity accounted investees and the identification and compensation of and transactions with key management personnel.</p>
<h3>(a) Group</h3>
<h4>Transactions</h4>
<p>Transactions between the Group and its related parties are made on terms equivalent to those that prevail in arm’s length transactions.</p>
<h5>Subsidiary undertakings</h5>
<p>The consolidated financial statements include the financial statements of the Company and its subsidiaries. A listing of all subsidiaries is provided in note 27. Sales to and purchases from subsidiary undertakings, together with outstanding payables and receivables, are eliminated in the preparation of the consolidated financial statements in accordance with IFRS 10 Consolidated Financial Statements.</p>
<h5>Equity accounted investees</h5>
<p>On 22 March 2013, the Group acquired 50% of the equity share capital of Wallaces Express Limited, a wholesaler of beverages in Scotland. The Group subsequently acquired the remaining 50% equity share capital of Wallaces Express Limited on 18 March 2014. The Group accounts for Wallaces Express Limited as a related party from date of the initial 50% investment, on 22 March 2013, to date of deemed disposal of this investment and subsequent acquisition of Wallaces Express Limited on 18 March 2014.</p>
<p>A subsidiary of the Group holds a 33% investment in Shanter Inns Limited with which the Group trades. Transactions between the Group and Shanter Inns are disclosed below.</p>
<p>On 21 March, 2012, the Group acquired a 25% equity investment in Maclay Group plc. The Maclay Group plc went into administration during the current financial year and the Group consequently impaired its investment in this entity, however the Group continues to trade with Maclay Inns Limited (in administration), a 100% owned subsidiary of the Maclay Group plc (in administration) and continues to account for it as a related party.</p>
<p>On 28 November 2012, the Group invested £0.3m (€0.4m at date of payment) in Thistle Pub Company Limited, a joint venture with Maclay Group plc.</p>
<p>During the current financial year, the Group entered into a joint venture arrangement with Heather Ale Limited, run by the Williams brothers who are recognised as leading family craft brewers in Scotland, to form a new entity Drygate Brewing Company Limited. The joint venture, which is run independently of the joint venture partners existing businesses, operates a craft brewing and retail facility adjacent to Wellpark brewery. The total investment was €0.5m.</p>
<p>The Group also holds a 50% investment in Beck &amp; Scott (Services) Limited (Northern Ireland) and a 45.61% investment in The Irish Brewing Company Limited (Ireland) following its acquisition of Gleeson. The Group traded with Beck &amp; Scott (Services) Limited (Northern Ireland) during the financial year as outlined below. The Group had no transactions with The Irish Brewing Company Limited (Ireland) which is a non-trading entity.</p>
<p>Loans extended by the Group to equity accounted investees are considered trading in nature and are included within advances to customers in Trade &amp; other receivables (note 15).</p>
