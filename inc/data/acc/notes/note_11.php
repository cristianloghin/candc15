<h3>Valuation of freehold land, buildings and plant &amp; machinery</h3>
<p>In the current financial year, the Group engaged the following external valuers to value the land &amp; buildings and plant &amp; machinery at the Group’s facilities in Clonmel, Wellpark, Shepton Mallet, Wallaces Express and Vermont;</p>
<ul class="list">
	<li>Shane O’Beirne RICS Registered Valuer (VRS) BSc (Surv) Dip AVEA MSCSI MRICS and Brian Gilson RICS Registered Valuer (VRS) BSc MSCSI MRICS FCI Arb - Lisney to value the freehold property at the Clonmel site;</li>
	<li>David Fawcett, FRICS RICS Registered Valuer – Sanderson Weatherall to value the plant and machinery at the Clonmel site;</li>
	<li>Timothy Smith BSc MRICS RICS Registered Valuer and Joseph Funtek BSc MRICS RICS Registered Valuer – Gerald Eve LLP to value the freehold property at the Shepton Mallet and Wellpark Brewery sites;</li>
	<li>Derek Elston FRCIS RICS Registered Valuer - Elston Sutton Industrial Appraisal Limited to value the plant and equipment at the Shepton Mallet and Wellpark Brewery sites;</li>
	<li>John Coto, Certified Machine &amp; Equipment appraiser, Alliance Machinery &amp; Equipment Appraisals to value the plant &amp; machinery at the Group’s Vermont site; and</li>
	<li>Martin Clarkson, BSc MRICS, RICS Registered Valuer – Gerald Eve LLP to value the land and buildings acquired on acquisition of Wallaces Express.</li>
</ul>
<p>The valuations were in accordance with the requirements of the RICS Valuation - Professional Standards, January 2014 edition and the International Valuation Standards.</p>
<p>The valuation of the land &amp; buildings in Clonmel was on the basis of market value, defined as ‘the price that would be received to sell an asset or paid to transfer a liability in an orderly transaction between market participants at the measurement date’ and was subject to the assumption that the property be sold as part of a continuing business. The valuers opinion of Fair Value of the Clonmel properties was primarily derived using comparable recent market transactions on an arm’s-length basis. The Fair Value of land &amp; buildings in Shepton Mallet and Wellpark Brewery were derived primarily based on the Depreciated Replacement Cost approach to valuation in light of the lack of comparative recent market transactions. </p>
<p>In view of the specialised nature of the Group’s plant &amp; machinery and the lack of comparable market evidence of similar plant being sold as a ‘going concern’, a Depreciated Replacement Cost approach was used to assess a Fair Value of the Group’s plant &amp; machinery. IAS16 Property, Plant and Equipment prescribes that where there is no market based evidence of Fair Value because of the specialist nature of the item of property, plant and equipment and the item is rarely sold, except as part of a continuing business, an entity may need to estimate Fair Value using an income or a Depreciated Replacement Cost approach to valuation.</p>
<p>The result of these external valuations, as at 28 February 2015, was a net increase in the value of land of €2.5m of which €2.7m was credited to the revaluation reserve with respect to an increase in the valuation of that element of the Group’s land where there was no revaluation decrease previously recognised on the same asset and €0.2m was expensed to the income statement as there was no previously recognised gain in the revaluation reserve against which to offset. The value of buildings decreased by a net €4.2m as a result of this valuation with €2.6m being credited to the revaluation reserve with respect to an increase in the value of an element of the Group’s buildings and which there was no revaluation decrease previously recognised on the same assets. This was offset by a reduction of €6.8m in the value of another element of the Group’s buildings which was expensed to the income statement as there was no previously recognised gain in the revaluation reserve against which to offset. The value of plant &amp; machinery was written down by a cumulative €3.5m which was expensed to the income statement as there was no previously recognised gain in the revaluation reserve against which to offset.</p>
<p>Also during the year, in light of a material reduction in the utilisation levels of a bottling line located at its cider manufacturing plant at Shepton Mallet used to bottle both own branded and third party branded product, a decision was taken to impair the bottling line by €3.3m.</p>
<p>On the acquisition of Wallaces Express the valuation of the land and buildings was on the basis of market value, defined as ‘the price that would be received to sell an asset or paid to transfer a liability in an orderly transaction between market participants at the measurement date’ and was subject to the assumption that the property be sold as part of a continuing business. The valuers opinion of Fair Value of the Wallaces Express properties was primarily derived using comparable recent market transactions on an arm’s-length basis.  This revaluation gave rise to a reduction in the carrying value of the land and buildings of €0.7m on acquisition as outlined in note 10.</p>
<p>For all other freehold land, buildings and plant &amp; machinery assets held by the Group an internal valuation was completed by the Directors as at 28 February 2015. As part of their valuation assessment, the Directors considered the following factors and their impact in determining year end valuation of the Group’s property, plant &amp; machinery:-</p>
<ul class="list">
	<li>Market fluctuations of land and industrial property prices since the date of the last external valuation,</li>
	<li>fluctuations driven by market commodity prices, of the gross replacement cost of property, plant &amp; machinery,</li>
	<li>projected asset utilisation rates based on FY2016 budgeted/forecasted production volumes,</li>
	<li>changes to functional and physical obsolescence of plant &amp; machinery beyond that which would normally be expected, and continued appropriateness of the assumed useful lives of property, plant &amp; machinery.</li>
</ul>
<p>Having considered the above variables, the Directors estimate that the changes arising from market fluctuations and anticipated utilisation rates would not result in a material change to the valuation of the carrying value of these items of property, plant &amp; equipment and hence no adjustment to their carrying value was deemed necessary.</p>
