<h3>Measurement techniques</h3>
<p>The Group used the following techniques to determine the fair value measurements categorised in Level 3:</p>
<ul class="list">
	<li>Land &amp; buildings in Ireland, US, Wallaces Express and Portugal and plant &amp; machinery located in Portugal are valued using a market value approach. The market value is the price that would be received to sell an asset or paid to transfer a liability in an orderly transaction between market participants at the measurement date.</li>
	<li>Land &amp; buildings located in the UK excluding Wallaces Express and plant &amp; machinery in the Group, excluding that located in Portugal, have been valued using the depreciated replacement cost approach. Depreciated replacement cost is assessed, firstly, by the identification of the gross replacement cost for each class of asset at each of the Group’s plants. A depreciation factor derived from both the physical and functional obsolescence of each class of asset, taking into account estimated residual values at the end of the life of each class of asset, is then applied to the gross replacement cost to determine the net replacement cost. An economic obsolescence factor, which is derived based on current and anticipated capacity or utilisation of each plant and machinery asset, at each of the Group’s plants, as a function of total available production capacity, is applied to determine the depreciated replacement cost.</li>
</ul>
