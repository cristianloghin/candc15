<h4>(a) Impairment of intangible assets</h4>
<p>To ensure that goodwill and brands considered to have an indefinite useful economic life are not carried at above their recoverable amount, impairment reviews are performed annually or more frequently if there is an indication that their carrying amount(s) may not be recoverable, comparing the carrying value of the assets with their recoverable amount using value-in-use computations. In the current financial year, as a result of such a review, the Group impaired the value of its intangible assets with respect to the US business by €150.0m as outlined in more detail in note 12.</p>
<h4>(b) Restructuring costs</h4>
<p>Restructuring costs of €2.8m comprising severance and other initiatives in the current financial year primarily relate to severance costs arising from a reorganisation programme in England &amp; Wales. In the prior financial year restructuring costs following the acquisition and integration of M. &amp; J. Gleeson (Investments) Limited (“Gleeson”) and its subsidiaries with the Group’s existing business and cost cutting initiatives undertaken at the Group’s manufacturing facilities resulted in an exceptional charge before tax of €6.7m. This charge was reduced by a defined benefit pension scheme curtailment gain of €0.6m due to the reduction in headcount numbers and the reclassification of these employees from active to deferred members. A curtailment gain arises where the value of the pension benefit of a deferred member is less than that of an active member.</p>
<h4>(c) Acquisition related expenditure</h4>
<p>The Group completed the acquisition of Green Light Brands Ltd., Monuriki Drinks Ltd., and Monuriki Sales and Marketing Ltd., (collectively referred to as “Green Light Brands”) during the current financial year, on 19 January 2015, for €3.2m. Green Light Brands was an external consultancy entity that provided sales and marketing services to the Group’s Shepton Mallet Cider Mill Brands while Monuriki had provided similar support to the Group’s International Wines and Spirits business. A decision was taken to bring these entities in-house as part of a rationalisation initiative of the Group’s sales and marketing structure. Also during the current financial year, the Group incurred €0.5m of costs directly attributable to the preliminary approach of the Spirit Pub Group. In the prior financial year acquisition costs of €1.1m were incurred which were directly attributable to the acquisitions of Gleeson, Biofun and VHCC. These costs primarily related to professional fees directly incurred in relation to the completion of these acquisitions.</p>
<h4>(d) Revaluation of property, plant &amp; equipment</h4>
<p>Property (comprising land and buildings) and plant &amp; machinery are valued at fair value on the balance sheet and reviewed for impairment on an annual basis. During the financial year, the Group engaged external valuers Shane O’Beirne, RICS (VRS) Registered Valuer, BSc (Surv) Dip AVEA MSCSI MRICS and Brian Gilson, BSc (Surv) MSCSI MRICS. FCI Arb – Lisney to value its freehold properties at the Group’s Clonmel site; David Fawcett, FRICS, IRRV (Hons) RICS Registered Valuer – Sanderson Weatherall to value its plant &amp; machinery at the Group’s Clonmel site, and, Timothy Smith BSc MRICS RICS Registered Valuer and Joseph Funtek BSc MRICS Registered valuer – Gerald Eve LLP to value its freehold properties at the Shepton Mallet and Wellpark Brewery sites, Derek Elston FRCIS RICS Registered Valuer – Elston Sutton Industrial Appraisal Limited to value the plant &amp; equipment at the Shepton Mallet and Wellpark Brewery sites and John Coto, Certified Machine &amp; Equipment appraiser, Alliance Machinery &amp; Equipment Appraisals to value the plant &amp; machinery at the Group’s Vermont site. Using the valuation methodologies as outlined in note 11, this resulted in a net revaluation loss of €10.5m accounted for in the income statement and a gain of €5.3 accounted for within other comprehensive income. Also during the year, in light of a material reduction in the utilisation levels of a bottling line located at the Group’s cider manufacturing plant at Shepton Mallet, used to bottle both own branded and third party branded product, a decision was taken to impair the bottling line by €3.3m.</p>
<h4>(e) Impairment of investment in equity accounted investee</h4>
<p>During the current financial year, the Group impaired its investment in the Maclay Group plc as a result of the Maclay Group plc entering administration proceedings during the financial year. This resulted in the impairment in the Group’s investment of €2.0m and the impairment of derivative financial instruments of €0.6m which were accounted for within finance expense.</p>
<h4>(f) Integration costs including write-off of redundant legacy IT assets</h4>
<p>During the current financial year, the Group incurred external consultancy fees and other costs of €2.2m directly attributable to the integration of Wallaces Express and Gleeson with the Group’s existing businesses. During the prior financial year, the Group incurred costs associated with the integration of the acquired Gleeson and VHCC businesses with the Group’s existing business. In addition, during the prior financial year, the Group wrote off redundant IT assets as a consequence of streamlining its IT system requirements following the acquisition and integration of both the Gleeson and VHCC businesses with the Group’s existing business.</p>
<h4>(g) Redeployment of bottling line</h4>
<p>In the prior financial year, a bottling line was redeployed from the Group’s Clonmel cider manufacturing plant to its Shepton Mallet cider manufacturing plant and costs of €6.6m were incurred in this regard. As a result of this deployment an existing PET line with a value of €0.8m in Shepton Mallet became redundant and was written off.</p>
<h4>(h) Profit on disposal of property, plant &amp; equipment</h4>
<p>In the current financial year the Group disposed of land &amp; buildings which were surplus to requirements realising a profit of €0.8m.</p>
<h4>(i) Other</h4>
<p>During the financial year ended 28 February 2009, the Group’s stock holding of apple juice at circa 36 months of forecasted future sales was deemed excessive in light of anticipated future needs, forward purchase commitments and useful life of the stock on hand. Accordingly the Group recorded an impairment charge in relation to excess apple juice stocks. During the current financial year, some of the previously impaired juice stocks were recovered and used by the Group. As a result this stock was written back to operating profit at its recoverable value resulting in a gain of €0.3m (2014: €nil). During the prior financial year, the Group incurred costs of €0.8m in relation to upgrading its listing on the Official List of the UK Listing Authority from a standard listing to a premium listing. Also included within Other in the prior financial year was a release of €0.3m with respect to an excess exit provision following the expiration of an onerous lease which originally arose from the consolidation of the Group’s Dublin offices in a previous financial year.</p>
<h4>(j) Foreign currency reclassified on deemed disposal of equity accounted investee</h4>
<p>On 18 March 2014, the Group announced the acquisition of the remaining 50% equity share capital of Wallaces Express Limited. Under IAS 28 Investments in Associates and Joint Ventures, this necessitated the deemed disposal of the Group’s initial 50% investment which was classified as an equity accounted investee and the recognition of the acquisition of control of the business under IFRS 3 Business Combinations. The Group had recognised €0.15m in the foreign currency reserve which was recycled to the income statement in the current financial year following this deemed disposal.</p>		