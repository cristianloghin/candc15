<p>Basic earnings per share is calculated by dividing the profit attributable to the ordinary shareholders by the weighted average number of ordinary shares in issue during the year, excluding ordinary shares purchased/issued by the Company and accounted for as treasury shares (at 28 February 2015: 16.5m shares; at 28 February 2014: 7.6m shares).</p>
<p>Diluted earnings per share is calculated by adjusting the weighted average number of ordinary shares outstanding to assume conversion of all potential dilutive ordinary shares. The average market value of the Company’s shares for purposes of calculating the dilutive effect of share options was based on quoted market prices for the period of the year that the options were outstanding.</p>
<p>Employee share awards (excluding awards which were granted under plans where the rules stipulate that obligations must be satisfied by the purchase of existing shares (note 4)), which are performance-based are treated as contingently issuable shares because their issue is contingent upon satisfaction of specified performance conditions in addition to the passage of time and continuous employment. In accordance with IAS 33 Earnings per Share, these contingently issuable shares are excluded from the computation of diluted earnings per share where the vesting conditions would not have been satisfied as at the end of the reporting period (2,164,448 at 28 February 2015 and 1,367,350 at 28 February 2014). If dilutive other contingently issuable ordinary shares are included in diluted EPS based on the number of shares that would be issuable if the end of the reporting period was the end of the contingency period.</p>