<p>All trade &amp; other receivables and advances to customers are monitored on an on-going basis for evidence of impairment and assessments are undertaken for individual accounts. A provision for impairment is created where the Group expects it may not be able to collect all amounts due in accordance with the original terms of the agreement with the customer. Balances included in the impairment provision are generally written off when there is no expectation of recovery. </p>
<p>Trade receivables are on average receivable within 47 days (2014: 47 days) of the balance sheet date, are unsecured and are not interest-bearing. An impairment provision is created in relation to advances to customers considered receivable in a period outside that originally contracted. The movement in the allowance for impairment in respect of trade receivables and advances to customers during the year was as follows:-</p>
