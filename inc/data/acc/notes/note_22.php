<h3>(d) Credit risk</h3>
<p>Credit risk is the risk of financial loss to the Group if a customer or counterparty to a financial instrument fails to meet its contractual obligations, and arises principally from the Group’s trade receivables, its cash advances to customers, cash &amp; cash equivalents including deposits with banks and derivative financial instruments contracted with banks. The Group has an indirect exposure to European Sovereigns via its defined benefit pension scheme investment portfolio. In the context of the Group’s operations, credit risk is mainly influenced by the individual characteristics of individual counterparties and is not considered particularly concentrated as it primarily arises from a wide and varied customer base; there are no material dependencies or concentrations of individual customers which would warrant disclosure under IFRS 8 Operating Segments.</p>
<p>The Group has detailed procedures for monitoring and managing the credit risk related to its trade receivables and advances to customers based on experience, customer track records and historic default rates. Generally, individual ‘risk limits’ are set by customer and risk is only accepted above such limits in defined circumstances. A strict credit assessment is made of all new applicants who request credit-trading terms. The utilisation and revision, where appropriate, of credit limits is regularly monitored. Impairment provision accounts are used to record impairment losses unless the Group is satisfied that no recovery of the amount owing is possible. At that point, the amount is considered irrecoverable and is written off directly against the trade receivable. The Group also manages credit risk through the use of a receivables purchase arrangement, for an element of its trade receivables. Under the terms of this arrangement, the Group transfers the credit risk, late payment risk and control of the receivables sold. The total receivables sold at 28 February 2015 was €21.4m.</p>
<p>Advances to customers are generally secured by, amongst others, rights over property or intangible assets, such as the right to take possession of the premises of the customer. Interest rates calculated on repayment/annuity advances are generally based on the risk-free rate plus a margin, which takes into account the risk profile of the customer and value of security given. The Group establishes an allowance for impairment of customers advances that represents its estimate of potential future losses.</p>
<p>From time to time, the Group holds significant cash balances, which are invested on a short-term basis and disclosed under cash &amp; cash equivalents in the balance sheet. Risk of counterparty default arising on short term cash deposits is controlled within a framework of dealing primarily with banks who are members of the Group’s banking syndicate, and by limiting the credit exposure to any one of these banks or institutions. Management does not expect any counterparty to fail to meet its obligations.</p>
<p>The Company also bears credit risk in relation to amounts owed by Group undertakings and from guarantees provided in respect of the liabilities of wholly owned subsidiaries as disclosed in note 25.</p>
