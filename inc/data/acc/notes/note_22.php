<p>Derivatives are initially recorded at fair value on the date the contract is entered into and subsequently re-measured to fair value at reporting dates. The gain or loss arising on re-measurement is recognised in the income statement except where the instrument is a designated hedging instrument under the cash flow hedging model.</p>
<h4>Cash flow hedges</h4>
<p>The Group, when appropriate, also enters into forward exchange contracts designated as cash flow hedges to manage short term foreign currency exposures to expected future sales. There were no outstanding contracts as at 28 February 2015 and 28 February 2014.</p>
<p>In order to qualify for hedge accounting, the Group is required to document the relationship between the item being hedged and the hedging instrument and demonstrate, at inception, that the hedge relationship will be highly effective on an ongoing basis. The hedge relationship must also be tested for effectiveness retrospectively and prospectively on subsequent reporting dates.</p>
<p>Gains and losses on cash flow hedges that are determined to be highly effective are recognised in other comprehensive income and then reflected in a cash flow hedging reserve within equity to the extent that they are actually effective. When the related forecasted transaction occurs, the deferred gains or losses are reclassified from other comprehensive income to the income statement. Ineffective portions of the gain or loss on the hedging instrument are recognised immediately in the income statement.</p>
<p>The Group ordinarily seeks to apply the hedge accounting model to all forward currency contracts.</p-->		