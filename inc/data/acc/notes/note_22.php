<h2>22. FINANCIAL INSTRUMENTS AND FINANCIAL RISK MANAGEMENT</h2>
<p>The Group’s multinational operations expose it to various financial risks in the ordinary course of business that include credit risk, liquidity risk, commodity price risk, currency risk and interest rate risk. This note discusses the Group’s exposure to each of these financial risks, summarises the risk management strategy for managing these risks and details the accounting treatment applied to the Group’s derivative financial instruments and hedging activities. The note is presented as follows:-</p>
<p>
	(a) Overview of the Group’s risk exposures and management strategy<br>
	(b) Financial assets and liabilities as at 28 February 2015 / 28 February 2014 and determination of fair value<br>
	(c) Market risk<br>
	(d) Credit risk<br>
	(e) Liquidity risk<br>
	(f) Accounting for derivative financial instruments and hedging activities
</p>
<h3>(a) Overview of the Group’s risk exposures and management strategy</h3>
<p>The most significant financial market risks that the Group is exposed to include foreign currency exchange rate risk, commodity price fluctuations, interest rate risk and financial counterparty creditworthiness. There has been no significant change during the financial year to either the financial risks faced by the Group or the Board’s approach to the management of these risks.</p>
<p>The Board of Directors has overall responsibility for the establishment and oversight of the Group’s risk management framework. This is executed through various committees to which the Board has delegated appropriate levels of authority. An essential part of this framework is the role undertaken by the Audit Committee, supported by the internal audit function, and the Group Chief Financial Officer. The Board, through its Committees, has reviewed the internal control environment and the risk management systems and process for identifying and evaluating the significant risks affecting the business and the policies and procedures by which these risks will be managed effectively. The Board has embedded these structures and procedures throughout the Group and considers these to be a robust and efficient mechanism for creating a culture of risk awareness at every level of management.</p>
<p>The Group’s risk management programme seeks to minimise the potential adverse effects, arising from fluctuations in financial markets, on the Group’s financial performance in a non speculative manner at a reasonable cost when economically viable to do so. The Group achieves the management of these risks in part, where appropriate, through the use of derivative financial instruments. All derivative financial contracts entered into in this regard are in liquid markets with credit rated parties. Treasury activities are performed within strict terms of reference that have been approved by the Board.</p>
