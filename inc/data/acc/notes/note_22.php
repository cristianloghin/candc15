<h3>Determination of Fair Value</h3>
<p>Set out below are the major methods and assumptions used in estimating the fair values of the Group’s financial assets and liabilities. There is no material difference between the fair value of financial assets and liabilities falling due within one year and their carrying amount as due to the short term maturity of these financial assets and liabilities their carrying amount is deemed to approximate fair value.</p>
<h5>Short term bank deposits and cash &amp; cash equivalents</h5>
<p>The nominal amount of all short-term bank deposits and cash &amp; cash equivalents is deemed to reflect fair value at the balance sheet date.</p>
<h5>Advances to customers</h5>
<p>The nominal amount of all advances to customers, after provision for impairment, is considered to reflect fair value.</p>
<h5>Trade &amp; other receivables/payables</h5>
<p>The nominal amount of all trade &amp; other receivables/payables after provision for impairment is deemed to reflect fair value at the balance sheet date with the exception of provisions and amounts due from Group undertakings after more than one year which are discounted to fair value.</p>
<h5>Derivatives (forward currency contracts, put/call options in equity accounted investees)</h5>
<p>The fair values of forward currency contracts, put/call options and interest rate swaps are based on market price calculations using financial models.</p>
<p>The Group has adopted the following fair value measurement hierarchy for financial instruments that are measured in the balance sheet at fair value:</p>
<ul class="list">
	<li>
		Level 1: quoted (unadjusted) prices in active markets for identical assets and liabilities.<br>
		<br>
		The fair value of financial instruments that are not traded in an active market (e.g. over the counter derivatives) are determined using valuation techniques. These valuation techniques maximise the use of observable market data where it is available and rely as little as possible on entity specific estimates.<br>
		<br>
	</li>	
	<li>
		Level 2: other techniques for which all inputs that have a significant effect on the recorded fair value are observable, either directly (i.e. as prices) or indirectly (i.e. derived from prices).<br>
		<br>
		The carrying values of any forward currency contracts held by the Group would be based on fair values arrived at using Level 2 inputs. There were no outstanding forward currency contracts held by the Group as at 28 February 2015 or 28 February 2014.<br>
		<br>
		As set out further in note 13, as part of the Group’s joint venture agreement in Thistle Pub Company Limited with Maclay Group plc during the financial year ended 28 February 2013, the Group granted Thistle Pub Company Limited and Maclay Group plc a call option enabling either of them to purchase the Group’s share of equity at a fixed price at any time in the first 15 years after the date the joint venture was formed, resulting in the recognition of a €0.2m financial liability. The carrying value of the option is valued based on Level 3 inputs, with the fair value being arrived at through the use of a Black-Scholes model. The movement in the fair value of this derivative to 28 February 2015 was less than €0.1m.<br>
		<br>
		Applying sensitivities to the key input assumptions used in valuing the above derivative financial instruments would not have a material impact on the carrying value of the derivative financial instruments or on the income statement.<br>
		<br>
	</li>
</ul>
<h5>Interest bearing loans &amp; borrowings</h5>
<p>The nominal amount of interest bearing loans &amp; borrowings is deemed to reflect fair value at 28 February 2015 due to the close proximity of draw down to the year end date.</p>
<h3>(c) Market risk</h3>
<p>Market risk is the risk that changes in market prices, such as commodity prices, foreign exchange rates and interest rates, will affect the Group’s income or the value of its holdings of financial instruments. The objective of market risk management is to manage and control market risk exposures within acceptable parameters, while optimising the return on risk.</p>
<p>The Group enters into derivative financial contracts, when deemed economically viable to do so, to mitigate risks arising in the ordinary course of business from foreign exchange rate and interest rate movements, and also incurs financial liabilities, in order to manage these market risks. The Group carries out all such transactions within the Treasury policy as set down by the Board of Directors.</p>
<h4>Commodity price risk</h4>
<p>The Group is exposed to variability in the price of commodities used in the production or in the packaging of finished products, such as barley, sugar, apple concentrate and aluminium. Commodity price risk is managed, where economically viable, through fixed price contracts with suppliers incorporating appropriate commodity hedging and pricing mechanisms. The Group does not directly enter into commodity hedge contracts. The cost of production is also sensitive to variability in the price of energy, primarily gas and electricity. It is Group policy to fix the cost of a certain level of its energy requirement through fixed price contractual arrangements directly with its energy suppliers.</p>
<h4>Currency risk</h4>
<p>The Company’s functional and reporting currency and that of its share capital is euro. The euro is also the Group’s reporting currency and the currency used for all planning and budgetary purposes. The Group is exposed to currency risk in relation to sales and purchase transactions by Group companies in currencies other than their functional currency (transaction risk), and fluctuations in the euro value of the Group’s net investment in foreign currency (sterling and US dollar) denominated subsidiary undertakings (translation risk). Currency exposures for the entire Group are managed and controlled centrally.</p>
<p>The Group seeks to minimise its foreign currency transaction exposure when economically viable by maximising the value of its foreign currency input costs and creating a natural hedge. Group policy is to manage its remaining net exposure by hedging a portion of the projected non-euro forecast sales revenue up to a maximum of two years ahead. Forward foreign currency contracts are used to manage this risk. The Group does not enter into such derivative financial instruments for speculative purposes. All such derivative contracts entered into are in liquid markets with credit-approved counterparties. Treasury operations are controlled within strict terms of reference that have been approved by the Board.</p>
<p>In addition, the Group has a number of long term US dollar and sterling intra group loans for which settlement is neither planned nor likely to happen in the foreseeable future, and as a consequence of which are deemed quasi equity in nature and are therefore part of the Group’s net investment in its foreign operations. The Group does not hedge the translation exposure arising on the translation of the profits of foreign currency subsidiaries.</p>
<p>The net currency gains and losses on transactional currency exposures are recognised in the income statement and the changes arising from fluctuations in the euro value of the Group’s net investment in foreign operations are reported separately within other comprehensive income.</p>
