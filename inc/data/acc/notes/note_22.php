<h3>(e) Liquidity risk</h3>
<p>Liquidity risk is the risk that the Group or Company will not be able to meet its financial obligations as they fall due. Liquid resources are defined as the total of cash &amp; cash equivalents. The Group finances its operations through cash generated by the business and medium term bank credit facilities; the Group does not use off-balance sheet special purpose entities as a source of liquidity or financing.</p>
<p>The Group’s policy is to ensure that sufficient resources are available either from cash balances, cash flows or committed bank facilities to meet all debt obligations as they fall due. To achieve this, the Group (a) maintains adequate cash or cash equivalent balances; (b) prepares detailed 3 year cash projections; and (c) keeps refinancing options under review. In addition, the Group maintains an overdraft facility that is unsecured.</p>
<p>In December 2014, the Group updated and amended its committed €450m multi-currency five year syndicated revolving loan facility with seven banks, namely Bank of Ireland, Bank of Scotland, Barclays Bank, Danske Bank, HSBC, Rabobank, and Ulster Bank, repayable in a single instalment on 22 December 2019. The facility agreement provides for a further €100m in the form of an uncommitted accordion facility and permits the Group to avail of further financial indebtedness, excluding working capital and guarantee facilities, to a maximum value of €150m, subject to agreeing the terms and conditions with the lenders. Consequently the Group is permitted under the terms of the agreement, to have debt capacity of €700m of which €342.8m was drawn at 28 February 2015 (2014: €309.6m was drawn under the Group’s 2012 multi-currency facility). This 5 year multi-currency facility replaces the Group’s previous multi-currency facility which was negotiated in February 2012 and which was due to mature in February 2017.</p>
<p>The Group’s debt facility incorporates two financial covenants:</p>
<ul class="list">
	<li>Interest cover: The ratio of EBITDA to net interest for a period of 12 months ending on each half year date will not be less than 3.5:1</li>
	<li>Net debt/EBITDA: The ratio of net debt on each half year date to EBITDA for a period of 12 months ending on a half year date will not exceed 3.5:1</li>
</ul>
<p>Compliance with these debt covenants is monitored continuously.</p>
<p>The Group’s main liquidity risk relates to maturing debt, however this risk is considered low at year end given the negotiation of a new five year committed facility in December 2014 as outlined above. </p>
<p>At the year end, the Group had net debt, net of unamortised issue costs, of €157.8m, with a Net debt/ EBITDA ratio of 1.1:1.</p>
