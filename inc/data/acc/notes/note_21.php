<h2>21. RETIREMENT BENEFIT OBLIGATIONS</h2>
<p>The Group operates a number of defined benefit pension schemes for certain employees, past and present, in the Republic of Ireland (ROI) and in Northern Ireland (NI), all of which provide pension benefits based on final salary and the assets of which are held in separate trustee administered funds. The Group closed its defined benefit pension schemes to new members in April 2007 and provides only defined contribution pension schemes for employees joining the Group since that date. The Group provides permanent health insurance cover for the benefit of certain employees and separately charges this to the income statement.</p>
<p>The defined benefit pension scheme assets are held in separate trustee administered funds to meet long-term pension liabilities to past and present employees. The trustees of the funds are required to act in the best interest of the funds’ beneficiaries. The appointment of trustees to the funds is determined by the schemes’ trust documentation. The Group has a policy in relation to its principal staff pension fund that members of the fund should nominate half of all fund trustees.</p>
<p>There are no active members remaining in the Executive defined benefit pension scheme (2014: no active members). There are 73 active members, representing &lt; 10% of total membership, in the ROI Staff defined benefit pension scheme (2014: 80 active members) and 4 active members in the NI scheme (2014: 5 active members). The Group’s ROI defined benefit pension reform programme concluded during the financial year ended 29 February 2012 with the Pensions Board issuing a directive under Section 50 of the Pensions Act 1990 to remove the mandatory pension increase rule, which guaranteed 3% per annum increase to certain pensions in payment, and to replace it with guaranteed pension increases of 2% per annum for each year 2012 to 2014 and thereafter for all future pension increases to be awarded on a discretionary basis.</p>
<h3>Actuarial valuations – funding requirements</h3>
<p>Independent actuarial valuations of the defined benefit pension schemes are carried out on a triennial basis using the attained age method. The most recent actuarial valuations of the ROI schemes were carried out with an effective date of 1 January 2012 while the date of the most recent actuarial valuation of the NI scheme was 31 December 2011. These valuations are currently being updated and are due to be completed by September 2015. The actuarial valuations are not available for public inspection; however the results of the valuations are advised to members of the various schemes.</p>
<p>The funding requirements in relation to the Group’s ROI defined benefit pension schemes are assessed at each valuation date and are implemented in accordance with the advice of the actuaries. Arising from the formal actuarial valuations of the main schemes on 1 January 2009 the schemes’ independent actuary, Mercer (Ireland) Limited, submitted Actuarial Funding Certificates to the Pensions Board confirming that the Schemes did not satisfy the Minimum Funding Standard at that date. Given that the removal of guaranteed pension increases would not correct this situation, Funding Proposals including an updated actuarial valuation were submitted to, and approved by the Pensions Board on 23 February 2012, which the Directors believe will enable the schemes to meet the Minimum Funding Standard by 31 December 2016. The Funding Proposals commit the Group to contributions of 14% of Pensionable Salaries to fund future pension accrual of benefits (previously 38.1% of Pensionable Salaries), a deficit contribution of €3.4m and an additional supplementary deficit contribution of €1.9m which the Group reserves the right to reduce or terminate on consultation with the Trustees, if the Scheme Actuary advises that it is no longer required due to a correction in market conditions. Funding Proposals cover the period to 31 December 2016. However, they will cease at an earlier date if the scheme funding target is met before then. The actuaries advised that as at 31 December 2014 the schemes were on track to meet the minimum funding standard and risk reserve by 31 December 2016, the end of the Funding Proposal period.</p>
<p>Following the 2011 actuarial valuation of the NI defined benefit pension scheme, a Schedule of Contributions and Recovery Plan was agreed committing the Group to annual contributions of £0.4m which the Directors believe will enable the scheme to meet the Statutory Funding Objective by June 2015.</p>
<p>The Group is exposed to a number of risks in relation to the funding position of these schemes, namely:-</p>
<p>Asset volatility: It is the Group’s intention to pursue a long term investment policy that emphasises investment in secure monetary assets to provide for the contractual benefits payable to members. The investment portfolio has exposure to equities, other growth assets and fixed interest investments the returns from which are uncertain and may fluctuate significantly in line with market movements. Assets held are valued at fair value using bid prices where relevant.</p>
<p>Discount rate: The discount rate is the rate of interest used to discount post-employment benefit obligations and is determined by reference to market yields at the balance sheet date on high quality corporate bonds with a currency and term consistent with the currency and estimated term of the Group’s post employment benefit obligations. Movements in discount rates have a significant impact on the value of the schemes’ liabilities.</p>
<p>Longevity: The value of the defined benefit obligations is influenced by demographic factors such as mortality experience and retirement patterns. Changes to life expectancy have a significant impact on the value of schemes’ liabilities.</p>
