<h4>Drygate Brewing Company Limited</h4>
<p>During the year, the Group entered into a joint venture arrangement with Heather Ale Limited, run by the Williams brothers who are recognised as leading family craft brewers&#160;in Scotland, to form a new entity Drygate Brewing Company Limited. The joint venture, which is run independently of the joint venture partners existing businesses, operates a craft brewing and retail facility adjacent to Wellpark brewery. The total investment was €0.5m. The financial result for the year attributable to the Group was a loss of €0.1m.</p>
<h4>Wallaces Express Limited</h4>
<p>On 22 March 2013, the Group acquired 50% of the equity share capital of Wallaces Express Limited (“Wallaces Express”), Scotland’s largest wines and spirits wholesaler, for €11.8m (£10.0m). Acquisition costs of €0.2m were also incurred in respect of the transaction.</p>
<p>Under the terms of this agreement, the Group entered into a call option arrangement enabling it to serve notice on Wallaces Express shareholders to acquire the remaining 50% of Wallaces Express at a predetermined price on 20 March 2015 or earlier at the Group’s option in the event of a breach of warranty by the Seller; and a put option granting Wallaces Express’ shareholders the right to serve notice on the Group to acquire the remaining 50% during the period January 2015 to March 2015 or earlier at the Sellers option in the event of a change of control, listing or insolvency of the buying company. The related derivative financial asset was valued at €1.2m while the related derivative financial liability was valued at €1.2m at 28 February 2014.</p>
<h5>Deemed disposal of equity accounted investee – initial investment in Wallaces Express Limited</h5>
<p>On 18 March 2014, the Group announced the acquisition of the remaining 50% equity share capital of Wallaces Express. Under IAS 28 Investments in Associates and Joint Ventures, this necessitated the deemed disposal of the Group’s initial 50% investment which was classified as an equity accounted investee and the recognition of the acquisition of control of the business under IFRS 3 Business Combinations.</p>
<p>The Group’s share of profits from initial acquisition of the equity accounted investee, on 22 March 2013, to date of deemed disposal on 22 March 2014 was €0.6m. In addition, the Group had recognised €0.15m in the foreign currency reserve which was recycled to the income statement in the current year following this deemed disposal.</p>
<h4>Maclay Group plc</h4>
<p>On 21 March 2012, the Group acquired a 25% equity investment in Maclay Group plc. The total cost of the investment was £2.1m (€2.5m euro equivalent at date of investment) of which £1.6m related to the value of the investment. Also included in the initial cost was a contracted derivative financial asset valued at £1.3m and a contracted derivative financial liability valued at £0.8m. The derivative financial asset related to a put option granted to the Group enabling it to sell its equity stake back to Maclay Group plc at a predetermined price at any time after the fifteenth anniversary of the acquisition, while the derivative financial liability related to the granting of a call option to Maclay Group plc enabling it to buy back the Group’s equity interest at a predetermined price at any time in the first fifteen years after the acquisition date. The Maclay Group plc went into administration during the current financial year and accordingly the Group has fully impaired its investment and related derivative financial instruments in this entity as at 28 February 2015. The financial result for the year attributable to the Group was less than €0.1m (2014: less than €0.1m).</p>
<h4>Thistle Pub Company Limited</h4>
<p>On 28 November 2012, the Group invested £0.3m (€0.4m euro equivalent at date of payment) in a joint venture with Maclay Group plc in Thistle Pub Company Limited. As part of the joint venture agreement, the Group granted Thistle Pub Company Limited and the Maclay Group plc a call option enabling either of them to purchase the Group’s share of the equity at a fixed price at any time in the first 15 years after the date the joint venture was formed. This call option has been valued at the acquisition date and resulted in the recognition of a £0.2m (€0.2m) financial liability. The movement in fair value of this derivative to 28 February 2015 was less than €0.1m (2014: less than €0.1m).</p>
<p>The joint venture purchased three public houses in the prior financial year and one public house in the current financial year. It now owns five public houses in total; all five public houses owned by the joint venture had opened and commenced trading as at 28 February 2015.</p>
<p>Unrealised gains arising from transactions with equity accounted investees are eliminated to the extent of the Group’s interest in the equity. Unrealised gains arising from the Group’s trading relationship with equity accounted investees as at the year end date was less than €0.1m (2014: less than €0.1m). Unrealised losses are eliminated in the same manner as unrealised gains, but only to the extent that there is no evidence of impairment in the Group’s interest in the entity.</p>
<h4>Other</h4>
<p>The Group also has an equity investment in Shanter Inns Limited, Beck &amp; Scott (Services) Limited (Northern Ireland) and The Irish Brewing Company Limited (Ireland). The value of these investments is less than €0.1m in the current and prior financial year.</p>
