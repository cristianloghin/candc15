<h2>4. SHARE-BASED PAYMENTS</h2>
<h3>Equity settled awards</h3>
<p>In April 2004, the Group established an equity settled Executive Share Option Scheme (ESOS) under which options to purchase shares in C&amp;C Group plc are granted to certain executive Directors and members of management. Under the terms of the scheme, the options are exercisable at the market price prevailing at the date of the grant of the option. The maximum grant that can normally be made to any individual in any one year is an award of 150% of base salary in that year. Options have been granted under this scheme in each year since 2004.</p>
<p>Under this scheme, options will not normally be exercisable until three years after the date of grant. In addition to continued employment, the options are subject to meeting a specific performance target relating to growth in earnings per share (EPS). EPS is calculated using earnings per share before exceptional items, as disclosed in the financial statements of the Group, subject to any further adjustments approved by the Remuneration Committee. This performance target requires that the Group’s aggregate EPS in the three financial years to be not less than the aggregate that would have been achieved had base-year earnings per share grown by 5% per annum in excess of the change in the Irish Consumer Price Index (Irish CPI) during the period, in order for options to vest. If after the relevant three-year period (i.e. 3 years from date of grant) the performance target is not met, the options lapse. In the current financial year, options awarded in May 2012 and May 2013 were deemed to be not capable of achieving their performance targets and consequently they were deemed to have lapsed in accordance with IFRS 2 Share Based Payment.</p>
<p>In April 2004, the Group established a Long Term Incentive Plan (Part I) (LTIP (Part I)) under the terms of which options to purchase shares in C&amp;C Group plc are granted at nominal cost to certain executive Directors and members of management. Under this plan, awards of up to 100% of base salary may normally be granted and up to 200% of base salary in exceptional circumstances.  The options will not normally be exercisable until three years after the date of grant.</p>
<p>Options under this scheme were granted in January 2006, in June of each year from 2006 through to 2008 and in each year since 2011. All awards granted prior to 2011 were forfeited, lapsed or did not vest. Options awarded in June 2011 and February 2012 were deemed to have only partially achieved their performance target in relation to earnings per share growth and consequently 85% of the outstanding awards lapsed in the prior financial year. In the current financial year the options granted in May 2012 and 2013 were deemed to be not capable of achieving their performance targets and consequently they were deemed to have lapsed in accordance with IFRS 2 Share Based Payment.</p>
<p>In addition to the time and continued employment vesting conditions, the Remuneration Committee has adopted performance conditions for the options awarded during each year since 2011 as follows:-</p>
<ul class="list">
	<li>With regard to 50% of the award, a performance condition relating to total shareholder return (TSR) applies and achievement of a financial underpin as mentioned below. 30% of this part of the award vests if the Group’s TSR over a three-year period equals the median TSR of a comparator group; 100% of this part of the award vests if the Group’s TSR over a three-year period equals or exceeds the TSR of the upper quartile of the comparator group; for performance between the median and the upper quartile there is straight-line pro-rating between 30% and 100%. None of this part of the award vests if the Group’s TSR over a three-year period is less than the median TSR of a comparator group. In respect of the TSR condition, a financial underpin applies; the growth in the Group’s earnings per share (EPS) over the three-year period must be 5% or more per annum in real terms (compared with Irish CPI) over the same period; alternatively the Remuneration Committee must be satisfied that the Group’s underlying financial performance warrants that level of vesting; otherwise the award lapses. EPS is calculated using earnings per share before exceptional items, as disclosed in the financial statements of the Group, subject to any further adjustments approved by the Remuneration Committee.</li>
	<li>With regard to the remaining 50% of the award, a performance condition relating to growth in EPS applies. 30% of this part of the award vests if the Group’s aggregate EPS in a three year period achieves 4% per annum compound growth in real terms (compared with Irish CPI). 100% of this part of the award vests if the Group’s aggregate EPS in a three year period achieves 10% per annum compound growth in real terms. There is straight-line pro-rating between 30% and 100% vesting for performance between 4% and 10% per annum compound real growth. None of this part of the award vests, if the real growth in the Group’s aggregate EPS in a three-year period is less than 4% per annum.</li>
</ul>		
<p>In December 2008, the Group established a Joint Share Ownership Plan (JSOP) whereby certain executive Directors and members of management were eligible to participate in the Plan at the discretion of the Remuneration Committee. Under this plan, Interests in the form of a restricted interest in ordinary shares in the Company were awarded to executive Directors and key members of senior management on payment upfront to the Company of an amount equal to 10% of the initial issue price of the shares on the acquisition of the Interest. The participants are also required to pay a further amount if the tax value of their Interest exceeds the price paid. When the further amount is paid, the Company compensates the participant for the obligation to pay this further amount by paying him an equivalent amount, which is, however, subject to income tax in the hands of the participant.</p>
<p>The vesting of Interests granted was subject to the following conditions. All of the Interests were subject to a time and service vesting condition with one-third of the Interest in the shares vesting on each of the first, second and third anniversary of acquisition, subject to continued employment only. In addition, half of the Interests in the shares were subject to a pre-vesting share price target. In order to benefit from those Interests the Company’s share price must have been greater than €2.50 for 13,800,000 of the Interests initially awarded, and €4.00 for an additional 2,200,000 of the Interests initially awarded, for at least 20 days out of 40 consecutive dealing days during the five-year period commencing on the date of acquisition of the Interest. All the Interests currently outstanding have now vested.</p>
<p>When an Interest vests, the trustees may, at the request of the participant and on payment of the further amount, if relevant, transfer shares to the participant of equal value to the participant’s Interest or the shares may be sold by the trustees, who will account to the participant for the difference between the sale proceeds (less expenses) and the Hurdle Value (balancing 90% of the acquisition price on the acquisition of the Interest).</p>
<p>In February 2010, the Group established a Restricted Share Award Scheme under the terms of which options to purchase shares in C&amp;C Group plc at nominal cost were granted to certain members of management, excluding executive Directors. The vesting conditions for these awards were similar to those for the award. All shares awarded under this scheme have now vested or lapsed.</p>
<p>In June 2010, the Group established a Recruitment and Retention Plan under the terms of which options to purchase shares in C&amp;C Group plc at nominal cost are granted to certain members of management, excluding executive Directors.</p>
<p>The performance conditions and/or other terms and conditions for awards granted under this plan are specifically approved by the Board of Directors at the time of each individual award, following a recommendation by the Remuneration Committee. The Board approved the award of 81,000 options under this plan in June 2010 and an award of 33,166 options in August 2011, in each case subject to time and service vesting conditions only so as to normally vest in three equal tranches, on the first, second and third anniversaries of grant and a further award of 31,791 options granted in August 2011 are also subject to time and service vesting conditions only, so as to normally vest on the third anniversary of grant.</p>
<p>In May 2012 and May 2013, awards of 1,036,255 and 252,672 respectively, were granted under the Recruitment and Retention Plan subject to continuous employment and the performance condition that the Company’s total shareholder return (“TSR”) must grow by not less than 25% between 17 May 2012 and 16 May 2014 for the May 2012 awards and between 16 May 2013 and 15 May 2015 for the May 2013 awards. Awards vest in full if the growth in TSR is at least 50% over that period and the Remuneration Committee is satisfied that the extent to which the award vests is appropriate given the general financial performance of the Group over the performance period. Where TSR growth is between 25% and 50% the percentage of the award that vests is calculated on a straight line basis between 25% and 100%. Options awarded in May 2012 were deemed to have only partially achieved their performance conditions and consequently 65% of the outstanding awards lapsed. Options granted in May 2013 were deemed to be not capable of achieving their performance conditions and consequently the outstanding awards were deeded to have lapsed in the current financial year under IFRS 2 Share Based Payment.</p>
<p>In the current financial year, 823,233 awards were granted in May 2014 and 283,092 awards were granted in January 2015 under the Recruitment and Retention plan. Of the May 2014 awards, 547,382 are subject to continued employment and the achievement of annual performance targets related to the business unit to which each recipient is aligned to. Options will vest in May 2017 on achievement of these conditions. Also in May 2014, an award of  92,111 was made subject to continued employment only, to vest in May 2016 and an award of 183,740 was also made subject to continued employment only to vest in May 2017. An award of 283,092 in January 2015 is subject to the continued employment of the recipient and also the achievement of performance targets linked to the business unit of the recipient. On achievement of both conditions the awards will vest in January 2018.</p>
<p>Obligations arising under the Restricted Share Award Scheme and the Recruitment and Retention Plan will be satisfied by the purchase of existing shares on the open market. On settlement, any difference between the amount included in the Share-based payment reserve account and the cash paid to purchase the shares is recognised in retained income via the statement of changes in equity.</p>
<p>In May 2011, the Group established a deferred equity settled share bonus scheme, Long Term Incentive Plan (Part II) (LTIP (Part II)), under which shares are awarded to certain employees (excluding executive Directors and senior management) at nominal cost, at the end of the financial year in which the award is granted, if the performance conditions set by the Remuneration Committee are achieved and subject to a two year time vesting period post the end of the relevant financial year. During the financial year ended 29 February 2012, the Remuneration Committee agreed two levels of award linked to operating profit targets. Based on the actual results to 29 February 2012, a right to receive shares at nominal cost equating to 23% of salary was granted to certain employees and a right to receive shares at nominal cost equating to 5% of salary was granted to other employees. The maximum number of shares over which awards were granted under the LTIP (Part II) in the financial year ended 29 February 2012 was set by reference to a share price of €3.55, being the closing share price on 18 May 2011, the date the results for the financial year ended 28 February 2011 were announced. Awards vested in May 2014, obligations are satisfied by the purchase of existing shares on the open market.</p>
<p>In November 2011, the Group set up Partnership and Matching Share Schemes for all ROI and UK based employees of the Group under the approved profit sharing schemes referred to below. Under these schemes, employees can invest in shares in C&amp;C Group plc (“partnership” shares) that will be matched on a 1:1 basis by the Company (“matching shares”) subject to Revenue approved limits. Both the partnership and matching shares are held on behalf of the employee by the Scheme trustee, Capita Corporate Trustees Limited. The shares are purchased on the open market on a monthly basis at the market price prevailing at the date of purchase with any remaining cash amounts carried forward and used in the next share purchase. The shares are held in trust for the participating employee, who has full voting rights and dividend entitlements on both partnership and matching shares. Matching shares may be forfeited and/or tax penalties may apply if the employee leaves the Group or removes their partnership shares within the Revenue-stipulated vesting period. The Revenue stipulated vesting period for matching shares awarded under the ROI scheme is three years and under the UK scheme is five years.</p>
<p>The Group held 218,455 matching shares (436,910 partnership and matching) in trust at 28 February 2015 (2014: 168,083 matching shares and 336,166 partnership and matching shares held).</p>
<p>In December 2011, the Group set up a discretionary Share Matching Plan under which invitations to participate were made to certain international (non ROI and UK) employees. Awards of shares (being a right to acquire shares at nominal cost) were made in February 2012, conditional on the participant agreeing to buy in advance and hold an equivalent number of ordinary shares in the Company (investment shares) in accordance with the plan. The maximum award was 325 shares per participant. Each award vested on the second anniversary of the grant date provided that the participant remained employed in the Group and had retained his/her investment shares acquired in relation to the matching award. Matching share awards were not entitled to dividends during the vesting period. Qualifying leavers remain entitled to their matching awards, which vested either on the date of cessation or on the normal vesting date, as the Group decided. Awards made to other leavers were forfeited. The February 2012 awards vested on 28 February 2014 and there were no subsequent awards.</p>
<p>There were no partnership and matching shares held by the Group in Trust, with respect to awards that had vested but had not yet been transferred to the participant, at 28 February 2015 (2014: 1,950 partnership and matching shares held).</p>
<h3>Cash-settled awards</h3>
<p>In January 2012, the Group granted 98,600 cash-settled awards on terms equivalent to the rules of the Recruitment and Retention Plan and subject to time and service vesting conditions only so as to normally vest, subject to continuous employment, on the third anniversary of date of grant. The award, which vested in the current financial year, was settled by way of a cash payment, calculated based on the closing price of the Group’s shares on the dealing day before the settlement date.</p>
<p>In May 2012, the Group granted 114,522 cash-settled awards on terms equivalent to the LTIP (Part I). These awards were deemed to be not capable of achieving their performance targets and consequently were deemed to have lapsed in accordance with IFRS 2 Share Based Payment.</p>
<p>In December 2012, the Group granted 150,786 cash-settled awards on terms equivalent to the rules of the Recruitment and Retention Plan. The awards are subject to continued employment and performance conditions linked to the achievement of annual performance targets with respect to the business unit to which the participant is aligned to. Each award, on vesting will be settled by way of a cash payment calculated based on the Group’s closing share price on the dealing day before the settlement date. The operating profit targets for the year ended 28 February 2015 and 28 February 2014 were deemed not to have been achieved and consequently the outstanding options at point of testing with respect to these elements have lapsed.</p>
<p>In July 2013, the Group granted 28,279 cash-settled awards on terms equivalent to the rules of the Recruitment and Retention Plan but subject to time and service vesting conditions only.</p>
<p>In the current financial year, the Group granted 16,723 cash-settled awards on terms equivalent to the rules of the Recruitment and Retention Plan. The awards are subject to continued employment and performance conditions linked to the achievement of performance targets with respect to the business unit to which the participant is aligned to. The award will vest in May 2017 on the achievement of these conditions.</p>
<h3>Award valuation</h3>
<p>The fair values assigned to the ESOS options granted were computed in accordance with a Black Scholes valuation methodology; the fair value of options awarded under the LTIP (Part I) and Recruitment and Retention Plan were computed in accordance with the stochastic model for the TSR element and the Black Scholes model for the EPS element; the fair value of options awarded under the LTIP (Part II) were computed in accordance with a Black Scholes model; and the fair value of the Interests awarded under the JSOP and the Restricted Share Award Plan were computed using a Monte Carlo simulation model.</p>
<p>As per IFRS 2 Share-based Payment, market based vesting conditions, such as the LTIP (Part I) and Recruitment and Retention Plan TSR condition and the share price target conditions in the JSOP and the Restricted Share Award Plan, have been taken into account in establishing the fair value of equity instruments granted. Non-market or performance related conditions were not taken into account in establishing the fair value of equity instruments granted, instead these non-market vesting conditions are taken into account by adjusting the number of equity instruments included in the measurement of the transaction amount so that, ultimately the amount recognised for time and services received as consideration for the equity instruments granted is based on the number of equity instruments that eventually vest, unless the failure to vest is due to failure to meet a market condition.</p>
